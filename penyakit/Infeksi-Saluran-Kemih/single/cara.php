                            Pengobatan Yang Bertujuan Untuk Meredakan Gejala Infeksi Saluran Kemih
                        </p>
                        <p>
                            Pada remaja yang sehat dan orang yang sudah dewasa dengan infeksi saluran kemih (ISK), maka pengobatan untuk infeksi saluran kemih (ISK) sangat diperlukan. Pada pria infeksi ini kemungkinan melibatkan kelenjar prostat dan pengobatan biasanya diberikan untuk durasi yang sedikit lebih lama. Tindak lanjut pengujian atau tes tidak diperlukan pada pria, pria yang  masih muda dan sehat atau wanita dengan infeksi kandung kemih (ISK) jika gejalanya sembuh.
                        </p>
