							Apa Yang Menjadi Penyebab Infeksi Saluran Kemih (ISK)?
						</p>
						<p>
							Infeksi saluran kemih (ISK) biasanya disebabkan oleh bakteri dari kotoran memasuki saluran kemih. Bakteri yang masuk melalui tabung yang membawa kencing keluar dari tubuh (uretra). Wanita yang memiliki uretra lebih pendek dibanding pria berarti bakteri lebih cenderung mencapai kandung kemih atau ginjal dan menyebabkan infeksi.
						</p>
						<p>
							Kasus infeksi saluran kemih (ISK) adalah sejenis bakteri yang disebut Escherichia coli (E. Coli). Bakteri ini biasanya hidup di usus dan disekitar anus. Bakteri E. Coli terbilang cukup tenang dilingkungan alami ususnya. Namun bakteri akan berkembang saat diperkenalkan ke keadaan asam urine. Infeksi saluran kemih (ISK) biasanya terjadi ketika bakteri E. Coli masuk ke air kencing dan mulai tumbuh. Infeksi biasanya mulai pada saat pembukaan uretra dimana urine meninggalkan tubuh dan bergerak ke atas ke saluran kemih sampai ke kandung kemih.
						</p>
						<p>
							Jika infeksi tidak diobati pada saat ini akan terus berlanjut dan segera menginfeksi ginjal. Bakteri E. Coli bisa bergerak cukup mudah dari daerah sekitar anus dan perineum hingga pembukaan uretra. Dua penyebab yang paling umum dari hal ini adalah penyeka dan hubungan seksual yang tidak tepat. Wanita yang lebih rentan terhadap infeksi saluran kemih (ISK) karena memiliki uretra yang lebih pendek yang memberi bakteri jalur lebih cepat ke kandung kemih.
						</p>
						<p>
							Proses yang normal pada saat buang air kecil (kencing) menyiram bakteri luar melalui uretra. Namun jika infeksi sudah berlangsung dan ada terlalu banyak bakteri, kemungkinan tidak menghentikan penyebarannya. Bahayanya adalah infeksi yang menyebar lebih jauh. Jika mencapai ginjal, bisa menyebabkan infeksi ginjal yang bisa menjadi kondisi yang sangat serius dan bahkan mengancam jika jika tidak segera diobati.
