							Peningkatan jumlah protein dalam urine bisa berarti memiliki masalah ginjal yang serius. Dan jika urine atau air kencing terasa sangat berbusa dan sebagian besar dokter kemungkinan akan menyarankan untuk melakukan tes urine untuk memeriksa kadar protein yang tinggi. Namun jika hasil dari tes nya positif, kemungkinan untuk melakukan tes lebih lanjut untuk menentukan penyebab utamanya.
