							Apa Yang Menyebabkan Kencing Berbusa?
						</p>
						<p>
							Yang menyebabkan kencing berbusa adalah kecepatan buang air nya sama seperti air busa yang saat keluar dari keran dengan cepat, air kencing atau urine akan berbusa jika menyentuh toilet dengan cepat. Busa juga harus dibersihkan dengan cepat. Namun terkadang urine juga bisa berbusa saat terkonsentrasi. Air kencing atau urine akan lebih terkonsentrasi jika Anda mengalami dehidrasi atau jarang minum.
						</p>
						<p>
							Ketika air kencing atau urine menunjukkan memiliki terlalu banyak protein seperti albumin dalam air kencing atau urine. Protein dalam urine bereaksi dengan udara untuk menghasilkan busa. Dan biasanya ginjal akan menyaring air ekstra dan mengeluarkan dari darah ke dalam air kencing atau urine Anda. Protein dan zat penting lainnya yang dibutuhkan tubuh Anda terlalu besar untuk disesuaikan dengan ginjal, dan tetap berada di aliran darah Anda.
						</p>
						<p>
							Jika ginjal Anda rusak, maka tidak bekerja dengan baik sebagaimana harusnya. Karena ginjal yang rusak dapat menyebabkan terlalu banyak kebocoran protein ke urine Anda dan ini disebut proteinuria (tanda penyakit ginjal kronis, yang dapat mengakibatkan diabetes, tekanan darah tinggi dan penyakit yang menyebabkan peradangan pada ginjal). Ini adalah tanda penyakit ginjal kronis atau stadium akhir kerusakan ginjal yang disebut stadium akhir penyakit ginjal. Penyebab urine atau air kencing yang kurang umum adalah ejakulasi retrograde yang merupakan kondisi yang terjadi pada pria saat air mani kembali ke kandung kemih dan bukannya dari penis.
						</p>
