							Apa Itu Infeksi Saluran Kemih (ISK)?
						</p>
						<p>
							Infeksi saluran kemih (ISK) adalah jenis infeksi yang paling umum yang mempengaruhi saluran kemih. Urine atau kencing adalah cairan yang disaring keluar dari aliran darah oleh ginjal. Urine biasanya mengandung bakteri. Saat bakteri masuk ke dalam kandung kemih atau ginjal dan berkembang biak dalam urine, maka infeksi saluran kemih (ISK) bisa terjadi.
						</p>
