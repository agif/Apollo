						<p class="font-weight-bold">
							Apa Gejala Pada Infeksi Saluran Kemih (ISK)?
						</p>

						<p>
							Infeksi kandung kemih (cystitis) atau uretra (tabung yang membawa urine keluar tubuh) dikenal dengan infeksi saluran kemih (ISK) yang dapat menyebabkan:
						</p>

						<div class="chitietbaiviet-lienquan container card">
							<ul>
								<li>Kebutuhan untuk buang air kecil yang sangat sering dari biasanya</li>
								<li>Sakit atau ketidaknyamanan saat buang air kecil</li>
								<li>Terdesak saat buang air kecil</li>
								<li>Merasa seolah-olah tidak dapat mengosongkan kandung kemih sepenuhnya</li>
								<li>Rasa sakit di perut</li>
								<li>Air kencing yang berbau busuk atau menagndung darah</li>
								<li>Merasa tidak sehat, pegal dan capek</li>
							</ul>
						</div>

						<p>
							Infeksi pada ginjal atau ureter (tabung yang menghubungkan ginjal dengan kandung kemih) dikenal sebagai infeksi saluran kemih (ISK) bagian atas. Dapat menyebabkan gejala:
						</p>

						<div class="chitietbaiviet-lienquan container card">
							<ul>
								<li>Suhu tinggi (demam)</li>
								<li>Sakit di sisi tubuh atau punggung</li>
								<li>Menggigil</li>
								<li>Kebingungan dan kegelisahan</li>
							</ul>
						</div>

						<p>
							Infeksi saluran kemih (ISK) yang lebih rendah sering terjadi dan biasanya bukan masalah yang utama. Infeksi saluran kemih (ISK) bagian atas bisa menjadi serius jika tidak diobati karena bisa merusak ginjal atau menyebar ke aliran darah.
						</p>
						
