						<p class="font-weight-bold">
							Apa Yang Menjadi Penyebab Infeksi Saluran Kemih (ISK)?
						</p>

						<p>
							Infeksi saluran kemih (ISK) terjadi ketika saluran kencing menjadi terinfeksi, biasanya oleh bakteri. Pada kebanyakan kasus bakteri dari usus memasuki saluran kemih melalui uretra (saluran yang menghubungkan kantung kemih ke lingkungan luar tubuh). Hal seperti ini kemungkinan terjadi saat menyeka bagian bawah atau saat berhubungan seksual. Berikut yang dapat meningkatkan risiko terkena infeksi saluran kemih (ISK):
						</p>

						<div class="chitietbaiviet-lienquan container card">
							<ul>
								<li>Kondisi yang menghalangi saluran kemih seperti batu ginjal</li>
								<li>Kesulitan mengosongkan kandung kemih sepenuhnya</li>
								<li>Penggunaan siafragma kontrasepsi atau kondom yang dilapisi spermisida (alat kontrasepsi yang mengandung bahan kimia yang digunakan untuk membunuh sperma)</li>
								<li>Diabetes (kencing manis)</li>
								<li>Sistem kekebalan tubuh yang lemah</li>
								<li>Kateter urine (sebuah tabung dikandung kemih yang digunakan untuk mengalirkan urine)</li>
								<li>Kelenjar prostat pada pria</li>
							</ul>
						</div>

						<p>
							Wanita kemungkinan lebih cenderung terkena infeksi menular seksual (IMS) karena uretra mereka lebih pendek dan lebih dekat ke anus, dari pada pria.
						</p>

