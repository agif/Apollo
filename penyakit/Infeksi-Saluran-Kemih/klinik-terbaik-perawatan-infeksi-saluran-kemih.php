                        <p>
                            Sebagian besar infeksi saluran kemih (ISK) pada wanita dimulai di saluran kemih bagian bawah, yang terdiri dari uretra (saluran yang menghubungkan kantung emih ke lingkungan luar tubuh) dan kandung kemih. Bakteri dari usus hidup di kulit dekat anus atau di vagina. Bakteri ini bisa mneyebar dan masuk ke saluran kemih melalui uretra (saluran yang menghubungkan kantung emih ke lingkungan luar tubuh).     
                        </p>
                        <p>
                            Jika mereka bergerak ke atas uretra (saluran yang menghubungkan kantung emih ke lingkungan luar tubuh), mereka kemungkinan menyebabkan infeksi kandung kemih (atau disebut dengan cystitis). Bakteri yang telah treinfeksi kandung kemih dapat melakukan perjalanan ke saluran kencing bagian atas, uretre dan ginjal. Infeksi pada ginjal disebut pielonefritis (infeksi ginjal atau  radang ginjal akibat infeksi bakteri).
                        </p>
                        <p>
                            Infeksi saluran kemih (ISK) bagian atas dapata menyebabkan penyakit yang lebih parah daripada infeksi saluran kemih (ISK) bagian bawah. Wanita yang kemungkinan lebih mungkin terkena dibandingkan dengan pria untuk terkena infeksi saluran kemih (ISK) karena uretra (saluran yang menghubungkan kantung emih ke lingkungan luar tubuh) lebih pendek pada wanita daripada pada pria. Pada wanita bakteri bisa mencapai kandung kemih lebih mudah
                        </p>

