                        <p class="font-weight-bold">
                            Bagaimana Cara Mencegah Infeksi Saluran Kemih Pada Wanita?
                        </p>

                        <p>
                            Jika Anda pernah mengalami infeksi saluran kemih (ISK) dan sangat menyakitkan lalu membuat frustasi terutama jika terus menerus berulang. Sementara perawatan umum lainnya hanya membersihkan infeksi saluran kemih (ISK) dalam beberapa hari.
                        </p>

                        <div class="chitietbaiviet-lienquan container card">
                            <ul>
                                <li class="red-text"><big>Minum banyak air putih dan sering buang air kecil (BAK atau kencing)</big>
                                    <p class="black-text">
                                        Cara termudah untuk mencegah infeksi saluran kemih (ISK) adalah dengan mneyingkirkan bakteri keluar dari kandung kemih dan saluran kencing sebelumdapat meresapnya
                                    </p>
                                </li>
                                <li class="red-text"><big>Mengusap dari depan ke belakang</big>
                                    <p class="black-text">
                                        Bakteri yang cenderung di anus, dan jika mencuci harus dari depan ke belakang terutama setelah buang air besar (BAB) dan tidak sampai ke uretra (saluran yang menghubungkan kantung kemih ke area luar tubuh)
                                    </p>
                                </li>

                                <li class="red-text"><big>Mencuci sebelum melakukan hubungan seksual dan buang air kecil setelah itu</big>
                                    <p class="black-text">
                                        Cuci area alat kelamin sebelum melakukan hubungan seksual agar membuat bakteri menjauh dari uretra (saluran yang menghubungkan kantung kemih ke area luar tubuh), saat buang air kecil juga mendorong bakteri yang masuk kembali ke saluran kemih
                                    </p>
                                </li>
                                <li class="red-text"><big>Hindari produk berbahan kimia</big>
                                    <p class="black-text">
                                        Lewat douching, semprotan deodoran atau spray vagina, wewangian vagina dan produk lainnya yang mengandung bahan kimia
                                    </p>
                                </li>
                                <li class="red-text"><big>Selalu mengontrol mengenai kelahiran</big>
                                    <p class="black-text">
                                        Kondom diafragma atau spermisida dapat membuat terkena infeksi saluran kemih (ISK) karena dapat berkontribusi terhadap pertumbuhan bakteri. Dan jika sering terkena infeksi saluran kemih (ISK) dan menggunakan salah satu metode kontrasepsi. Hindari pakaian ketat yang bisa menjebak bakteri di dekat uretra.
                                    </p>
                                </li>
                            </ul>
