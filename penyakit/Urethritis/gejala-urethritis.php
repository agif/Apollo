                                Tanda Dan Gejala Pada Pasien Dengan Urethritis Termasuk:
                            </p>

                            <div class="chitietbaiviet-lienquan container card">
                                <ul>
                                    <li>Uretral discharge: Jenis cairan selain urine atau air mani yang keluar dari pembukaan penis. Kemungkinan akan berwarna kuning, hijau, cokelat atau diwarnai dengan darah. Produksi yang tidak berhubungan dengan aktifitas seksual.</li>
                                    <li>Disuria (Buang Air Kecil Terasa Sakit): Biasanya teralokasi pada meatus (pembukaan uretra) atau distal penis yang terburuk saat pagi dan diperparah oleh konsumsi alkohol yang berlebih. Biasnaya tidak ada frekuensi dan urgensi kencing.</li>
                                    <li>Adanya sensasi rasa gatal pada uretra (saluran yang menghubungkan kantung kemih ke lingkungan luar tubuh) atau iritasi antara rongga mulut.</li>
                                    <li>Siklus haid yang memburuk (kadang-kadang).</li>
                                    <li>Gejala sistemik (misalnya demma, keidnginan, keringat, mual).</li>
                                </ul>
                            </div>

                            <p>
                                Jika tanda dan gejala terjadi, biasanya mereka muncul dalam waktu 2-4 minggu setelah kontak dengan infeksi, namun kadang kala dapat muncul dalam satu atau 2 hari (tergantung penyebab radang). Dalma kasus yang ringan, gejala kemungkinan tidak muncul selama beberapa bulan. Jika Anda mendapatkan tanda dan gejala yang mungkin harus diperhatikan adalah:
