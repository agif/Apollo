                                    <li>
                                        <p>Gejala utama urethritis adalah adanya rasa sakit atau rasa terbakar saat buang air kecil (kencing) dan keinginna untuk buang air kecil (kencing) lebih sering atau urgensi kencing. Gejala lainnya adalah adanya kemerahan disekitar pembukaan uretra (saluran yang menghubungkan kantung kemih ke lingkungan luar tubuh) atau tabung yang membawa air kencing dari kandung kemih ke bagian luar tubuh. Pria dengan urethritis gonokokal juga sering mengeluarkan cairan kuning dari uretra (saluran yang menghubungkan kantung kemih ke lingkungan luar tubuh).</p>
