                                Pada kebanyakan orang yang menderita urethritis kemungkinan juga tidak memiliki gejala yang nyata. Hal seperti ini terutama berlaku untuk semua wanita. Namun pada pria, gejalanya kemungkinan tidak tampak jelas jika urethritis berkembang sebagai akibat dari klamidia atau terkadang infeksi trikomoniasis. Untuk hal seperti ini, sangat penting untuk menjalani tes jika Anda terinfeksi dengan infeksi menular seksual (IMS).
