                                Pengobatan Urethritis Bertujuan Untuk Mengobati Penyebab Urethritis Dalam Pencegahan Penyebaran Infeksi
                            </p>

                            <p>
                                Menggunakan pengaman atau kondom saat berhubungan seksual sangat membantu mengurangi risiko urethritis. Pengobatan akan tergantung pada penyebab utamanya dan jika pasien memiliki infeksi bakteri. Dokter akan menyarankan beberapa pengobatan, sementara gejala bisa diobati. Untuk menghilangkan adanya rasa sakit dan bengkak diperlukan pengobatan anti inflamasi (respon biologis kompleks dari jaringan vaskuler atas adanya bahaya, seperti pathogen (mikroorganisme parasit), kerusakan sel atau iritasi) dan ini adalah salah satu usaha perlindungan diri tubuh kita untuk menghilangkan rangsangan penyebab luka dan inisiasi proses penyembuhan jaringan.
                            </p>
                            <p>
                                Jika tidak segera diobati maka gejalanya bisa saja menjadi lebih memburuk. Hal seperti ini juga dapat menyebabkan jaringan parut (bagian dari proses penyembuhan alami) terbentuk di uretra (saluran yang menghubungkan kantung kemih ke lingkungan luar tubuh), dan menyebabkannya menyempitt dan bisa menyebabkan penyakit radang panggul (pelvic inflammatory disease atau PID).
