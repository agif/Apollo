                            <p class="font-weight-bold">
                                Pengobatan Urethritis Pada Wanita
                            </p>
                            <p>
                                Adanya infeksi urethritis pada wanita karena strain bakteri tertentu telah menjadi resisten terhadap pengobatan tertentu. Dokter akan menyarankan pengobatan lebih lanjut ketika gejalanya sudah menyebar. Semua pasangan seks dari seseorang yang terinfeksi urethritis menular harus segera diobati. Ketika Anda sedang melakukan pengobatan untuk penyakit urethritis hindari untuk melakukan hubungan seksual sebelum pengobatan selesai dan dinyatakan sembuh oleh dokter.
                            </p>
                            <p>
                                Tidak ada pengobatan yang secara khusus yang diperlukan untuk penyakit urethritis yang disebabkan oleh luka atau iritasi kimia. Ada juga penyakit urethritis yang disebabkan oleh infeksi trichomonas atau trikomoniasis. Selain itu, pasangan seksual Anda juga harus segera diobati guna untuk mencegah reinfeksi. Sangat penting untuk dilakukan pengujian atau tes laboratorium setelah 3 bulan untuk memastikan infeksi benar-benar hilang serta pasangan Anda yang diobati.
                            </p>
                            <p>
                                Jika infeksi menular seksual (IMS) menyebabkan infeksi sangat penting bahwa semua pasangan seksual menjalani pemeriksaan dan pengujian atau tes laboratorium jika diperlukan. Ini untuk mencegah adanya penyebaran infeksi menular seksual (IMS) dan reinfeksi. Ketika gejala mulai membaik namun Anda harus tetap menyelesaikan pengobatan yang direkomendasikan oleh dokter, karena jika tidak diselesaikan maka infeksi akan menjadi lebih buruk.
                            </p>

