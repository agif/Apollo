                                Berapa Biaya Untuk Pengobatan Urethritis Pada Wanita?
                            </p>
                            <p>
                                Urethritis adalah pembengkakan pada uretra (saluran yang menghubungkan kantung kemih ke lingkungan luar tubuh). Itulah tabung yang membawa air kencing dari kandung kemih ke luar tubuh. Rasa nyeri dengan buang air kecil (kencing) adalah gejala utama drai penyakit urethritis. Urethritis biasanya disebabkan oleh infeksi bakteri. 
                            </p>
                            <p>
                                Pada setiap wanita penyakit urethritis kemungkinan disebabkan oleh iritasi. Misalnya menggosok atau menekan area genital dari pakaian ketat atau seks bisa menyebabkan urethritis. Bisa juga karena aktifitas fisik seperti bersepeda. Iritasi seperti sabun, dan spermisida atau penyebab lainnya. Pada kebanyakan wanita yang lebih tua, jaringan uretra (saluran yang menghubungkan kantung kemih ke lingkungan luar tubuh)dan kandung kemih menjadi lebih tipis dan kering setelah menopause. Hal ini disebabkan oleh kekurangannya hormon estrogen wanita. Ini adalah salah satu penyebab urethritis biasa pada wanita yang lebih tua.
                            </p>
                            <p>
                                Untuk mencari tempat pengobatan yang sesuai dengan apa yang kita inginkan sangatlah harus difikirkan secara baik-baik. Karena pengobatan yang dilakukan berhubungan dengan organ seksual. Kesehatan organ seksual sangat penting untuk kehidupan dan kesuburan setiap manusia termasuk pria maupun wanita. Oleh karena itu, harus menjaga kebersihan dan kesehatan organ seksual agar terhindar dari berbagai penyakit kelamin.
                            </p>
                            <p>
                                Pengobatan penyakit kelamin Anda bisa temukan di Klinik Utama Sentosa. Klinik Utama Sentosa menangani berbagai jenis penyakit kelamin seperti penyakit menular seksual (PMS), infeksi menular seksual (IMS), Andrologi dan Ginekologi. Jika Anda menderita urethritis segera lakukan pemeriksaan dengan dokter, agar segera ditindak lanjuti. 
                            </p>
                            <p>
                                Sehingga penyakit nya tidak akan semakin parah dan segera disembuhkan sebelum penyakitnya semakin bertambah parah. Karena jika penyakitnya sudah semakin bertambah parah maka pengobatannya juga akan semakin lama tentunya biaya pengobatan yang harus dikeluarkan juga tidak akansedikit harus memerlukan biaya yang banyak untuk menyembuhkan penyakitnya.
                            </p>

