                            <p>
                                Bakteri dan virus dapat menyebabkan urethritis. Bakteri yang sama yang menyebabkan kondisi seperti ini adalah e. coli, klamidia dan gonore. Bakteri ini juga menyebabkan infeksi saluran kemih dan beberapa penyakit menular seksual (PMS). 
