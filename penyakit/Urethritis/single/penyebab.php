                            <p class="font-weight-bold">
                                Apa Yang Menjadi Penyebab Urethritis?
                            </p>
                            <p>
                                Urethritis kemungkinan disebabkan oleh bakteri, jamur, atau virus (misalnya virus herpes simpleks virus atau HSV). Penyakit menular seksual seksual (PMS) adalah penyebab umum urethritis. Organisme seperti neisseria gonorrhoeae yang menyebabkan gonore atau kencing nanah bisa menyebar ke uretra saat melakukan hubungan seksual dengan pasangan yang terinfeksi. Klamidia dan herpes simpleks virus (HSV) juga sering ditularkan secara seksual dan dapat menyebabkan urethritis.
                            </p>
                            <p>
                               Ketika pria menyebarkan urethritis, organisme gonore adalah penyebab yang sangat umum. Meskipun organisme ini dapat menginfeksi urethra pada wanita, vagina, serviks (leher rahim), rahim, ovarium (indung telur) dan saluran tuba lebih mungkin terinfeksi. Trichomonas, sejenis parasit mikroskopik juga menyebabkan urethritis pada pria. Urethritis mungkin juga disebabkan oleh bakteri yang umumnya menyebabkan infksisaluran kemih (ISK) lainnya seperti escherichis coli atau e. coli.
                            </p>
                            <p>
                                Bakteri atau virus adalah penyebab paling umum. Bakteri penyebab urethritis adalah sama yang menyebabkan infeksi kandung kemih dan ginjal. Virus yang menyebabkan urethritis termasuk Human Papilloma Virus (HPV), Herpes Simpleks Virus (HSV) dan sitomegalovirus. Infeksinya biasa menempel di uretra (saluran yang menghubungkan kantung kemih ke lingkungan luar tubuh), tapi bisa ditransfer ke organ reproduksi wanita yang pada gilirannya menyebabkan penyakit radang panggul (PID). 
                            </p>
                            <p>
                                Kelainan penyakit radang panggul (PID) dan epididimitis (peradangan saluran di belakang testis yang menyimpan dan membawa sperma) dapat menyebabkan infertilitas atau kemandulan pada kedua jenis kelamin. Penyebab urehtiris lainnya juga termasuk cedera serta kepekaan terhadap bahan kimia yang sering digunakan pada spermisida (alat kontrasepsi yang mengandung bahan kimia yang digunakan untuk membunuh sperma) serta alat kontrasepsi. 
                            </p>

