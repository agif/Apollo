                            <p class="font-weight-bold">
                                Apa Yang Menjadi Risiko Penyebab Urethritis
                            </p>
                            <div class="chitietbaiviet-lienquan container card">
                                <ul>
                                    <li>Menjadi perempuan</li>
                                    <li>Menjadi laki-laki usia 20 sampai 35 tahun</li>
                                    <li>Memiliki banyak pasangan seksual</li>
                                    <li>Perilaku seksual berisiko tinggi (seperti seks anak tanpa kondom)</li>
                                    <li>Sejarah penyakit menular seksual (PMS)</li>
                                </ul>
                            </div>

                            <p>
                                Bakteri yang terkait dengan penyakit menular seksual (PMS) klamidia dan gonore terutama menyebabkan urethritis. Gonetokokus urehtritis yang biasa ditularkan melalui kontak seksual. Risiko tertular urethritis meningkat pada wanita di masa reproduksi mereka dan juga untuk pria berusia 25-35 tahun karena aktifitas seksual meningkat. Pasien dengan banyak pasangan seksual dan riwayat penyakit menular seksual (PMS) sebelumnya juga cenderung mengalami kontak urethritis. Pria dan wanita dapat mengurangi risiko urehtitis mereka dengan mempraktikan seks yang aman seperti kondom dan monogami.
                            </p>

                            <p class="font-weight-bold">
                                Infeksi Yang Dapat Menyebabkan Urethritis Meliputi:
                            </p>

                            <div class="chitietbaiviet-lienquan container card">
                                <ul>
                                    <li>Klamidia (43 % kasus urehtritis pada pria disebabkan oleh bakteri penyebab klamidia)</li>
                                    <li>Gonore atau kencing nanah (urethritis juga bisa disebabkan oleh bakteri penyebab gonore atau kencing nanah dan pada kasus tersebut disebut gonococcal urehtiris)</li>
                                    <li>Trichomoniasis (infeksi menular seksual atau IMS disebabkan oleh parasit kecil)</li>
                                    <li>Infeksi saluran kemih (ISK)</li>
                                    <li>Herpes Simpleks Virus (HSV) yang bisa menyebabkan luka dan herpes genital</li>
                                    <li>Virus yang disebut adenovirus yang biasanya menyebabkan sakit tenggorokan atau infeksi mata</li>
                                </ul>
                            </div>

                            <p class="font-weight-bold">
                                <em>Hal seperti ini juga disebabkan oleh infeksi, misalnya:</em>
                            </p>

                            <div class="chitietbaiviet-lienquan container card">
                                <ul>
                                    <li>Iritasi menggunakan produk yang berbahan kimia, atau spermisida didekat uretra</li>
                                    <li>Kerusakan disebabakan oleh seks atau masturbasi yang gencar</li>
                                    <li>Meremas atau mengotak-atik uretra</li>
                                    <li>Kerusakan disebabakan karena memasukan benda ke dalam urera (seperti kateter)</li>
                                </ul>
                            </div>

                            <p>
                                Pada pria, urethritis berasal dari penyakit menular seksual (PMS). Dua penyakit menular seksual (PMS) yang paling umum dikaitkan dengan urethritis pada pria adalah klamidia dan gonore. Namun penyakit menular seksual (PMS) lain juga dapat menyebabkan gejala ini termasuk herpes simpleks virus (HSV) dan virus papiloma manusia. 
                            </p>
                            <p>
                                Pada sebagain besar wanita yang lebih tua, jaringan uretra dan kandung kemih menjadi lebih tipis dan kering setelah menopause karena kurangnya hormon estrogen wanita. Penyebab urehtritis biasa pada wanita yang lebih tua terkadang infeksi vagina (misalnya infeksi jamur). Urehtritis kadang-kadang hanya iritasi yang disebabkan oleh gesekan atau tekanan pada area selangkangan. Misalnya pakaian ketat bisa menyebabkan urehtritis.
                            </p>

