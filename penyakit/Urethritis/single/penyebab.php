                            <p class="font-weight-bold">
                                Bakteri, Virus Dan Protozoa Umum Yang Menyebabkan Urethritis Termasuk:
                            </p>
                            <p>
                               Urethritis adalah infeksi uretra yang kemungkinan disebabkan oleh beberapa spesies bakteri, virus dan protozoa.
                            </p>
                            
                            <big class="red-text">Bakteri yang menyebabkan infeksi menular seksual (IMS) seperti:</big>
                            <div class="chitietbaiviet-lienquan container card">
                                <ul>
                                    <li>Neisseria gonorrhoeae</li>
                                    <li>Chlamydia trachomatis</li>
                                    <li>Mycoplasma genitalium</li>
                                    <li>Ureaplasma urealyticum</li>
                                    <li>Escherichia coli yang ada disaluran anus atau tinja</li>
                                </ul>
                            </div>

                            <big class="red-text">Virus yang menyebabkan urethritis termasuk:</big>
                            <div class="chitietbaiviet-lienquan container card">
                                <ul>
                                    <li>Herpes Simpleks Virus (HSV) yang menyebabkan infeksi menular seksual (IMS)</li>
                                    <li>Adenovirus terutama jika pasien memiliki mata merah</li>
                                </ul>
                            </div>

                            <big class="red-text">Protozoa yang menyebabkan infeksi uretra meliputi:</big>
                            <div class="chitietbaiviet-lienquan container card">
                                <ul>
                                    <li>Trichomonas vaginalis yang menyebabkan infeksi menular seksual (IMS)</li>
                                </ul>
                            </div>

                            <big class="red-text">Dan beberapa penyebab lain dari urethritis adalah:</big>
                            <div class="chitietbaiviet-lienquan container card">
                                <ul>
                                    <li>Cedera pada uretra</li>
                                    <li>Sensitivitas terhadap bahan kimia dalam spermisida atau kontrasepsi lainnya</li>
                                </ul>
                            </div>

