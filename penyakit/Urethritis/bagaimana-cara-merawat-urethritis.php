                                Perawatan Urethritis Pada Wanita
                            </p>
                            <p>
                                Adanya infeksi urethritis pada wanita karena strain bakteri tertentu telah menjadi resisten terhadap perawatan tertentu. Dokter akan menyarankan perawatan lebih lanjut ketika gejalanya sudah menyebar. Semua pasangan seks dari seseorang yang terinfeksi urethritis menular harus segera diobati. Ketika Anda sedang melakukan perawatan untuk penyakit urethritis hindari untuk melakukan hubungan seksual sebelum perawatan selesai dan dinyatakan sembuh oleh dokter.
                            </p>
                            <p>
                                Tidak ada perawatan yang secara khusus yang diperlukan untuk penyakit urethritis yang disebabkan oleh luka atau iritasi kimia. Ada juga penyakit urethritis yang disebabkan oleh infeksi trichomonas atau trikomoniasis. Selain itu, pasangan seksual Anda juga harus segera diobati guna untuk mencegah reinfeksi. Sangat penting untuk dilakukan pengujian atau tes laboratorium setelah 3 bulan untuk memastikan infeksi benar-benar hilang serta pasangan Anda yang diobati.
                            </p>
                            <p>
                                Jika infeksi menular seksual (IMS) menyebabkan infeksi sangat penting bahwa semua pasangan seksual menjalani pemeriksaan dan pengujian atau tes laboratorium jika diperlukan. Ini untuk mencegah adanya penyebaran infeksi menular seksual (IMS) dan reinfeksi. Ketika gejala mulai membaik namun Anda harus tetap menyelesaikan perawatan yang direkomendasikan oleh dokter, karena jika tidak diselesaikan maka infeksi akan menjadi lebih buruk.
                            </p>

