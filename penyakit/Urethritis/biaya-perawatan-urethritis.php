                                Sehingga penyakit nya tidak akan semakin parah dan segera disembuhkan sebelum penyakitnya semakin bertambah parah. Karena jika penyakitnya sudah semakin bertambah parah maka Perawatannya juga akan semakin lama tentunya biaya perawatan yang harus dikeluarkan juga tidak akansedikit harus memerlukan biaya yang banyak untuk menyembuhkan penyakitnya.
