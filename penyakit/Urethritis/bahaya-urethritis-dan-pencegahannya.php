                        <p class="font-weight-bold">
                            Pencegahan Terbaik Untuk Penyakit Urethritis
                        </p>

                        <div class="chitietbaiviet-lienquan container card">
                            <ul>
                                <li>Menyeka bagian depan ke belakang, karena bakteri akan terjadi sebaliknya dan akan menyebabkan infeksi saluran kemih (ISK)</li>
                                <li>Selalu mengosongkan kandung kemih ketika ingin buang air kecil (BAK atau kencing)</li>
                                <li>Dan dipastikan ketika kencing benar-benar mengosongkan kandung kemih</li>
                                <li>Meminum banyak air putih (mineral) minimal minum 8 gelas per hari karena setiap orang dengan penyakit ginjal atau masalah kesehatan lainnya kemungkinan kekurangan minum air putih (mineral). Dan segera konsultasikan dengan dokter mengenai semua keluhan Anda.</li>
                                <li>Selalu waspada terkena diabetes (kencing manis) dan selalu hindari terkena infeksi saluran kemih (ISK) yang berpotensi mengancam kehidupan.</li>
                            </ul>
