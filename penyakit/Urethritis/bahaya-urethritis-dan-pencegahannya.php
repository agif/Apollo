                        </p>

                        <p>
                            Faktor-faktor yang dapat meningkatkan risiko terkena sindrom uretra (saluran yang menghubungkan kantung kemih ke lingkungan luar tubuh):
                        </p>

                        <div class="chitietbaiviet-lienquan container card">
                            <ul>
                                <li>Memiliki infeksi kandung kemih atau ginjal yang disebabkan oleh bakteri</li>
                                <li>Obat-obatan tertentu</li>
                                <li>Berhubungan seks tanpa pengaman atau kondom</li>
                                <li>Tertularnya infeksi menular seksual (IMS)</li>
                                <li>Terlibat dalam hubungan seksual</li>
