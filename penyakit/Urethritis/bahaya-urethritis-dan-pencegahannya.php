                        <p class="font-weight-bold">
                            Bahaya Jika Urethritis Tidak Diobati
                        </p>
                        <p>
                            Jika urethritis tidak diobati maka kemungkinan akan mengalami pembengkakan buah pelir (epididimitis) dan infeksi kelenjar prostat serta kemungkinan dapat menginfeksi pasangan seksualnya. Ada beberapa faktor utama yang dapat menyebabkan serangan akut urethritis selama kehamilan, diantaranya meliputi:
                        </p>

                        <div class="chitietbaiviet-lienquan container card">
                            <ul>
                                <li>Penetrasi infeksi melalui tindakan seksual</li>
                                <li>Hipotermia (suatu kondisi dimana mekanisme tubuh untuk pengaturan suhu kesulitan mengatasi tekanan suhu dingin)</li>
                                <li>Penyakit menular seksual (PMS) atau infeksi</li>
                                <li>Reaksi alergi</li>
                                <li>Cacat pada sistem genitourinari</li>
                                <li>Kelelahan sebagai akibat kelelahan emosional atau fisik</li>
                                <li>Konsumsi air dalam jumlah minimum</li>
                                <li>Kurangnya kebersihan yang cukup</li>
                            </ul>
                        </div>

                        <p>
                            Urethritis gonococcus adalah patogen atau parasit yang paling berbahaya yang mampu mencapai janin dan menyebabkan kematiannya. Ada kemungkinan untuk mengembangkan proses persalinan secara prematur. Dan agar terhindar dari konsekuensi berat bila terjadi hal-hal yang negatif disarankan untuk segera melakukan konsultasi dengan dokter.
                        </p>
                        <p>
                            Bukan penyakit berbahaya yang bersifat non infeksi namun bisa menimbulkan komplikasi. Jadi harus diobati untuk menyembuhkan radang nya bukan untuk mengabaikannya. Diagnsosis biasanya terdiri dari beberapa tahap, tahap awal riwayat kesehatan secara lengkap, termasuk setiap keluhan yang ada, gaya hidup dan kehamilan. Kemudian di spesifikasikan antara penyakit akut dan kronis.
                        </p>
                       
