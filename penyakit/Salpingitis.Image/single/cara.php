                            <p class="font-weight-bold">
                                Pengobatan Salpingitis Untuk Setiap Pasangan Seksual
                            </p>

                            <p>
                                Setiap pasangan seksual diharuskan melakukan pengujian atau pengecekan infeksi menular seksual (IMS). Jika hanya Anda yang membersihkan infeksi namun tetap melakukan hubungan seksual dengan pasangan yang belum diobati, maka infeksinya akan terus kembali kepada Anda. Adanya infeksi dapat menyebabkan abses (bisul atau kantong sempit berisi nanah yang berkumpul pada jaringan, organ, atau ruang didalam tubuh), dan diperlukan tindakan operasi laparoskopi untuk mengurasnya.
                            </p>
                            <p>
                                Jika infeksi telah menyebabkan bekas luka yang terbentuk, maka dokter akan merekomendasikan untuk dilakukan pembedahan atau operasi untuk mengangkat area yang telah rusak. Dokter akan cenderung merekomendasikan opersi jika suatu saat nanti Anda menginginkan kehamilan. Apabila saluran tuba terisi cairan, dengan dilakukannya operasi untuk menguras cairan atau mengeluarkan area yang berisi cairan.
                            </p>

