                            <p class="font-weight-bold">
                                Pengobatan Untuk Salpingitis
                            </p>
                            <p>
                                Salpingitis adalah sejenis penyakit radang panggul (PID). Penyakit radang panggul (PID) mengacu pada infeksi organ reproduksi. Ini berkembang ketika bakteri berbahaya memasuki saluran reproduksi. Salpingitis dan bentuk penyakit radang panggul (PID) lainnya biasanya diakibatkan oleh infeksi menula seksual (IMS) yang melibatkan bakteri seperti klamidia atau gonore atau kencing nanah.
                            </p>
                            <p>
                               Salpingitis menyebabkan radang saluran tuba. Peradangan bisa menyebar dengan mudah dari satu tabung ke tabung lainnya, sehingga kedua tabung bisa menjadi terpengaruh. Jika tidak diobati maka salpingitis bisa mengakibatkan komplikasi jangka panjang. Salpingitis adalah radang tabung atau tuba fallopi dan berhubungan dengan penyakit radang panggul. Biasanya disebabkan oleh infeksi bakteri menular seksual, infeksi lain yang dapat menyebabkan salpingitis meliputi mycoplasma, staphylococcus dan streptococcus. 
                            </p>
                            <p>
                                Salpingitis dapat didiagnosis sebagai salpingitis akut atau kronis. Salpingitis akut terjadi ketika saluran tuba menjadi meradang dan mengeluarkan cairan dan menyebabkan dinding dalam menempel satu sama lain dan ke jaringan terdekat lainnya. Salpingitis kronis adalah bentuk peradangan yang lebih ringan yang tetap ada setelah serangan akut. Biasanya berlangsung lebih lama namun menghasilkan lebih sedikit gejala.
                            </p>

