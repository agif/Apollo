							Apa Yang Menjadi Penyebab Bartholinitis?
						</p>
						<p>
							Penyebab kista bartholin adalah cadangan cairan. Cairan bisa menumpuk saat pembukaan kelenjar (duktus) menjadi terhambat dan kemungkinan disebabkan oleh infeksi atau luka. Kista bartholin bisa terinfeksi dan membentuk abses. Sejumlah bakteri dapat menyebabkan infeksi termasuk escherichia coli (e. coli) dan bakteri yang menyebabkan infeksi menular seksual seperti gonore (kencing nanah) dan klamidia.     
						</p>
						<p>
							Bartholinitis lebih sering disebabkan oleh anaerob non sporeforming, gonococcus atau staphylococcus, kurang umum streptococcus, e. coli, trichomoniasis dan infeksi campuran. Kelenjar bartholin yang juga dikenal sebagai kelenjar vestibular (sistem yang berperan besar dalam keseimbangan tubuh, gejala gangguan keseimbangan tersebut bisa berupa badan goyah, pusing atau seperti memutar dan mengambang) utama atau sepasang kelenjar diantara vagina dan vulva (bagian organ seksual eksternal wanita yang merupakan area yang mengelilingi lubang kencing dan vagina) yang menghasilkan pelumasan saat dirangsang.
						</p>
						<p>
							Seiring dengan kelenjar vestibular yang lebih rendah, akan membantu dalam masalah hubungan seksual dengan mengurangi gesekan. Cairan pelumas mengalir dari kelenjar bartholin ke tabung kecil (duktus) yang panjangnya sekitar 0.8 inci (2 m) ke bagian bawah pintu masuk vagina. Jika terjadi penyumbatan disaluran ini, pelumasnya menumpuk. Saluran berkembang dan terbentuk kista. Ini adalah kista bartholin. Bila kista terbentuk, ada risiko infeksi didaerah tersebut dan abses berikutnya.
						</p>

