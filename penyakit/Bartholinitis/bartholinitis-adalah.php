							Kelenjar bartholin adalah sepasang kelenjar yang ditemukan tepat di belakang dan kedua sisi bibir yang mengelilingi pintu masuk ke vagina. Kelenjar bartholin mengeluarkan cairan sebagai pelumas saat berhubungan seks. Kelenjar bartholin mengeluarkan cairan yang berperan sebagai pelumas saat berhubungan seksual. Cairan yang bergerak ke tabung kecil yang disebut duktus, masuk ke dalam vagina.Jika saluran ini tersumbat, bisa mengisi dengan cairan dan melebar untuk membentuk kista atau peradangan ekstrim.Mengapa saluran tersebut tersumbat, namun beberapa kasus terkait dengan infeksi bakteri menular seksual seperti gonore atau kencing nanah atau klamidia dan infeksi bakteri lainnya seperti escherichia coli (e.coli).
