                        </p>

                        <p>
                            Dalam kasus atau prosedur ini selaput dara (hymen) dan bagian lainnya dari daerah alat kelamin wanita diperbaharui. Prosedur ini dilakukan dalam kasus bibir alat kelamin wanita telah kehilangan dan menjadi datar karena hubungan seksual secara berulang. Selain itu, bibir alat kelamin wanita yang lebih kecil dan dalam atau labia minora menjadi lebih besar dan menonjol.
                        </p>

