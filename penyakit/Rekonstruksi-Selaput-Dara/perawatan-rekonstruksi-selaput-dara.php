                            Bagaimana Cara Merawat Rekonstruksi Selaput Dara?
                        </p>

                        <p>
                            Rekonstruksi selaput dara atau hymenoplasty dapat dilakukan untuk memperbaiki selaput dara (hymen) yang robek yang merupakan membran tipis yang terletak di alat kelamin wanita. Selama prosedur berlangsung maka jahitan halus yang digunakan untuk memperbaiki sisa-sisa selaput dara (hymen) yang robek. Dilakukan dibawah anestesi lokal.
                        </p>
                        <p>
                            Anda harus menghindari aktifitas berat seperti menunggang kuda, bersepeda, berhubungan seksual, dan olahraga berat setidaknya selama pemulihan dan dibawah pengawasan dokter. Setelah prosedur hymenoplasty dilakukan maka sangat penting untuk melakukan perawatan dan membuat beberapa perubahan dalam gaya hidup.
                        </p>
                        <p>
                            Perlu memastikan agar alat kelamin wanita harus tetap bersih untuk membantu menghindari segala jenis kotoran yang berada di daerah yang luka. Jangan pernah melanjutkan aktifitas seksual sampai rasa sakitnya dan keputihannya sembuh. Hindari mengangkat benda berat dan pekerjaan rumah tangga yang berat karena akan memberi tekanan pada seluruh area alat kelamin.
                        </p>
                        <p>
                            Segala jenis tekanan dapat merusak selaput dara (hymen) yang akan menyebabkan rasa sakit. Jauhkan pikiran dari tekanan dan ketegangan yang kemungkinan dialami. Hindari pekerjaan berat selama pemulihan. Istirahat secara baik untuk menyembuhkan luka, sehingga semakin banyak beristirahat maka akan semakin cepat untuk pemulihan dan dapat melanjutkan aktifitas sehari-hari Anda.
                        </p>
                        <p>
                            Makanan yang baik dapat memberikan tubuh Anda energi dan nutrisi yang dibutuhkan untuk pemulihan. Jika semuanya berjalan dengan baik tanpa adanya komplikasi, maka saat Anda melakukan hubungan seksual untuk yang pertama kalinya, Anda akan mengalami perdarahan dan mengalami ketidaknyamanan.
                        </p>

