                            <p class="font-weight-bold">
                                Bagaimana Cara Mengobati Adnexitis?
                            </p>

                            <p>
                                Segera hentikan penggunaan produk yang dapat menyebabkan munculnya iritasi dan penggunaan pakaian dalam katun yang membuat tidak dapat bernapas atau sesak. Untuk mengurangi iritasi, gatal dan gejala Adnexitis lainnya lakukan pemeriksaan dengan dokter. Jika tidak segera ditangani maka akan mengakibatkan kondisi yang lebih serius lagi seperti kanket vulva yang sangat parah.
                            </p>

                            <p>
                                Dengan mengurangi stress dan cukup tidur serta makan makanan yang bergizi juga merupakan cara yang baik untuk pengobatan diri sendiri dan mencegah adanya gejala Adnexitis selanjutnya. Adapun cara lain untuk mencegah  penyakit Adnexitis termasuk memakai celana berbahan katun dan jaga kebersihan area vagina, dan menghindari douching vagina. Selalu gunakan pengaman atau kondom selama aktifitas seksual untuk mengurangi adanya risiko penyakit menular seksual (PMS) dan infeksi vagina lainnya.
                            </p>

