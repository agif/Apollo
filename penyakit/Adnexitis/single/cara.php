                                Langkah pertama untuk pengobatan Adnexitis secara medis adalah dengan mendapatkan diagnosis. Dilihat dari apa yang menjadi penyebabnya, dengan pengobatan awal akan meringankan atau meredakan rasa gatal yang parah. Jika penyakit Adnexitis berlanjut dan memburuk, kemungkinan akan dilakukan biopsi (pengambilan jaringan tubuh untuk pemeriksaan laboratorium)vulva untuk menghindari kemungkinan adanya kanker atau kondisi vulva kronis yang bisa menyebabkan gatala, memar, nyeri dan jaringan parut. 
