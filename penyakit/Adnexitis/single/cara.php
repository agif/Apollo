                                Pengobatan Adnexitis Yang Utama
                            </p>
                            <p>
                                Adnexitis adalah radang dan infeksi pada vulva, organ luar vulva termasuk labia majora dan minora, klitoris dan kelenjar vestibular. Gejala dasar Adnexitis adalah kemerahan, pembengkakan pada kulit vulva. Adanya sensasi gatal adalah gejala yang sangat menonjol dan konsisten. Area kulit vulva yang terkena bisa berubah menjadi warna putih dan adanya retakan atau lepuhan yang berisi cairan yang pecah dan berkerak.
                            </p>
                            <p>
                                Karena Adnexitis adalah gejala, maka pengobatan Adnexitis sangat bervariasi sesuai dengan apa yang menjadi penyebabnya. Namun ada pegobatan yang bisa digunakan untuk mengurangi ketidaknyamanan penderitanya. Saat berhubungan seksual gunakan pelumas yang tidak mengganggu dan jangan gunakan produk yang kemungkinan akan mengakibatkan iritasi. Jaga agar vulva (bagian organ seksual eksternal wanita yang merupakan area yang mengelilingi lubang kencing dan vagina)tetap bersih, selalu mengeringkannya dan pastikan untuk membersihkan daerah vagina dan perianal sepenuhnya selama periode menstruasi dan setelah adanya buang air besar (BAB).
                            </p>

