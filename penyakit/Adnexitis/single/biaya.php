                                Berapa Biaya Untuk Pengobatan Adnexitis?
                            </p>
                            <p>
                                Berbeda-beda jenis penyakit yang dimiliki dan diderita setiap orang, maka berbeda-beda pula jenis pemeriksaan dan pengobatan yang harus dilewati dan dijalani. Oleh karena itu, sudah pasti biaya yang harus dikeluarkan setiap orang juga berbeda-beda. Hingga dari yang masih terjangkau sampai biaya pengobatan yang sangat mahal. Maka dari itu jaga kesehatan diri Anda, sebelum penyakit menghampiri Anda.
                            </p>
                            <p>
                                Penyakit Adnexitis adalah salah satu kelompok penyakit yang menyerang organ seksual. Adnexitis adalah terjadinya peradangan pada vulva (bagian organ seksual eksternal wanita yang merupakan area yang mengelilingi lubang kencing dan vagina) yang diakibatkan oleh adanya alergi, cedera atau iritasi eksternal. Biasanya masyarakat awam akan mengabaikan pada setiap gejala yang ada, hingga pada akhirnya penyakitnya semakin parah dan baru diobati. Semakin ditunda-tunda pengobatannya maka penyakitnya akan semakin parah dan biaya pun akan semakin mahal.
                            </p>
                            <p>
                                Jika Anda mengalami gejala Adnexitis seperti rasa gatal yang ekstrem dan konstan, terasa panas seperti sensasi terbakar didaerah vulva, keputihan pada vagina, adanya retakan kecil pada kulit vulva, terdapat kemerahan dan pembengkakan pada vulva (bagian organ seksual eksternal wanita yang merupakan area yang mengelilingi lubang kencing dan vagina) dan labia (bibir vagina), terjadi pelepuhan pada vulva(bagian organ seksual eksternal wanita yang merupakan area yang mengelilingi lubang kencing dan vagina), scaly atau bercak putih pada vulva(bagian organ seksual eksternal wanita yang merupakan area yang mengelilingi lubang kencing dan vagina). Maka segera konsultasikan dengan dokter agar segera ditindak lanjuti dan diketahui diagnosisnya. Setelah itu, lalu dilakukan pengobatan sesuai petunjuk dokter.
                            </p>

