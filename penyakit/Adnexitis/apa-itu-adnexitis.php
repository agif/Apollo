							Apa Yang Menjadi Komplikasi Pada Adnexitis?
						</p>
						<p>
							Komplikasi adnexa (organ reproduksi wanita berupa jaringan yang berada disamping kanan dan kiri melekat pada rahim, yang bernama tuba fallopi dan ovarium atau indung telur) paling parah dan paling umum adalah ketidaksuburan. Sedangkan bagi wanita hamil, selama ovulasi (proses yang terjadi didalam diklus menstruasi wanita) telur harus dibuahi oleh sperma dan hal ini terjadi di saluran tuba. Jika seorang wanita menderita adnexitis, tuba fallopi menyempit dan ada kemungkinan telur tidak dilepaskan dan untuk alasan ini wanita tersebut tidak dapat hamil secara alami.
						</p>

