							Apa Yang Dimaksud Dengan Adnexitis?
						</p>
						<p>
							Adnexitis atau salpingo oophoritis adalah proses inflamasi pelengkap uterus (organ reproduksi yang utama pada kebanyakan wanita), yaitu ovarium (indung telur) dan tuba fallopi (tabung fallopi). perawatan adnexa (organ reproduksi wanita berupa jaringan yang berada disamping kanan dan kiri melekat pada rahim, yang bernama tuba fallopi dan ovarium atau indung telur) yang adekuat dan tepat sangat penting untuk mencegah ketidaksuburan.
						</p>
						<p>
							Adnexitis adalah infeksi ginekologis yang hanya menyerang wanita. Saluran tuba adalah organ yang terlibat dan berada di rongga pelvis (panggul) yang dekat dengan ovarium (indung telur) yang bergabung dengan rongga perut. Saluran tuba sangat penting untuk kesuburan wanita. Jika ovarium (indung telur) meradang, saluran tuba biasanya juga terkena.
						</p>
						<p>
							Penyakit ini salag satu yang paling umum di kalangan wanita usia subur. Penyakit ini menyakitkan bagi seorang wanita, mengurangi kualitas hidup dan yang sangat parah dan terbengkalai bahkan bisa menyebabkan ketidaksuburan. Dengan adnexa (organ reproduksi wanita berupa jaringan yang berada disamping kanan dan kiri melekat pada rahim, yang bernama tuba fallopi dan ovarium atau indung telur), pembentukan inflamasi yang hampir simultan terjadi, baik di ovarium (indung telur) dan di tuba fallopi. 
						</p>

