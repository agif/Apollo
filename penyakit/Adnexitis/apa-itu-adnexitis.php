							Penyakit radang panggul (PID) sering disebabkan oleh perilaku seksual yang berisiko tinggi yang menyebabkan bakteri yang melakukan perjalanan dari organ genital wanita dan menyerang tuba fallopi. Peradangan dapat berkembang dan hubungan seksual dengan demikian merupakan faktor risiko nomor satu. Transmisi yang kurang umum adalah melalui organ perut yang terinfeksi seperti radang usus buntu. Demikian pula, kemungkinan infeksi yang rendah terjadi saat persalinan atau aborsi. Secara keseluruhan,sistem kekebalan tubuh yang rendah dapat meningkatkan risiko adnexa (organ reproduksi wanita berupa jaringan yang berada disamping kanan dan kiri melekat pada rahim, yang bernama tuba fallopi dan ovarium atau indung telur).
