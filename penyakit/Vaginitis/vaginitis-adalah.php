                                    <li>Trikomoniasis atau parasit</li>
                                    <li>Vaginitis tidak menular</li>
                                </ul>
                            </div>

                            <p>
                                Tidak semua infeksi yang menyebabkan vaginitis adalah penyakit menular seksual (PMS) namun ada beberapa penyakit menular seksual (PMS) yang dapat menyebabkan vaginitis.
                            </p>
                           
