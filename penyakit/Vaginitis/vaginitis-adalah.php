                                Apa itu Vaginitis ?
                            </p>

                            <p>
                                Vaginitis adalah pembengkakan, pembakaran, gatal atau infeksi pada vagina. Bila vulva (bagian organ seksual eksternal wanita yang merupakan area yang mengelilingi lubang kencing dan vagina) terkena maka disebut vulvovaginitis (peradangan pada vagina yang dapat menghasilkan keputihan, gatal dan nyeri).
                            </p>
                            <p>
                                Vulva adalah lipatan kulit di sekitar pembukaan vagina. Vaginitis adalah masalah yang sangat umum yang dapat terjadi pada wanita dari segala usia. Vaginitis adalah peradangan pada vagina.Vaginitis sering merupakan hasil infeksi ragi, bakteri atau trichomonas namun iritasi fisik atau kimia pada daerah tersebut bisa menjadi penyebabnya. Ada jenis vaginitis diantaranya:
                            </p>

                            <div class="chitietbaiviet-lienquan container card">
                                <ul>
                                    <li>Bakteri vaginosis</li>
