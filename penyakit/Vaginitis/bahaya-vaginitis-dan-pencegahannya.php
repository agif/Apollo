                        </p>
                        <p>
                            Ketika Anda yang berhubungan seks dengan seseorang yang pernah memiliki vaginosis bakteri, serta memiliki banyak pasangan seksual atau dengan pasangan seks yang selalu baru, maka akan berisiko terkena infeksi. Namun douching juga bisa meningkatkan risiko karena mengganggu keseimbangan bakteri didalam vagina.Komplikasi dan risiko dengan seseorang yang memiliki vaginosis bakteri bisa sangat serius jika infeksinya tidak diobati. Dan vaginosis yang tidak diobati akan menyebabkan:
                        </p>

