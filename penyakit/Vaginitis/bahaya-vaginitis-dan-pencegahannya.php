                        <p class="font-weight-bold">
                            Apakah Berbahaya Berhubungan Seksual Ketika Terkena Vaginitis?
                        </p>

                        <p>
                            Berhubungan seks ketika memiliki infeksi vagina atau vaginitis (peradangan vagina) kemungkinan akan memiliki ketidaknyamanan dan kemungkinan akan membuat gejalanya menjadi memburuk. Sumber infeksi berasal dari infeksi menular kronis (kandidiasis atau infeksi jamur yang biasanya pada kulit atau selaput lendir yang disebabkan oleh kandida), kemungkinan dari hubungan seks yang tidak memperdulikan gejala yang akan atau terus memburuk hanya karena masih merasa nyaman. Dan penyebab paling umum dari infeksi vagina adalah:
                        </p>

                        <div class="chitietbaiviet-lienquan container card">
                            <ul>
                                <li>Infeksi ragi (kandidiasis) atau infeksi jamur yang biasanya pada kulit atau selaput lendir yang disebabkan oleh kandida</li>
                                <li>Bakteri vaginosis (pertumbuhan bakteri yang berlebihan di vagina)</li>
                                <li>Trikomoniasis (penyakit menular seksual atau PMS yang disebabkan oleh parasit)</li>
                            </ul>
                        </div>

                        <p>
                            Infeksi ragi dan vaginosis bakteri bukanlah infeksi menular seksual (IMS) namun memiliki pasangan seksual yang selalu baru dapat meningkatkan risiko bakteri vaginosis. Namun untuk trikomoniasis adalah infeksi menular seksual (IMS) dan untuk mencegahnya kemungkinan akan terjadi reinfeksi, hindari melakukan hubungan intim atau seksual sampai Anda dan pasangan Anda selesai melakukan perawatan dan tidak adanya lagi gejala.
