                            <p class="font-weight-bold">
                                Pengobatan Yang Disebabkan Oleh Apa Yang Menjadi Penyebabnya:
                            </p>

                            <div class="chitietbaiviet-lienquan container card">
                                <ul>
                                    <li><b class="red-text">Trikomoniasis</b>
                                        <p>
                                            Penyakit vaginitis yang disebabkan oleh trikomoniasis yang menyebabkan adanya kemerahan dan adanya pembengkakan daerah vulva (bagian organ seksual eksternal wanita yang merupakan area yang mengelilingi lubang kencing dan vagina). Pasangan seksual harus melakukan pengobatan vaginitis secara bersamaan. Selain itu pasiaen harus menghindari hubungan seksual minimal 1 minggu sampai pasangan tersebut dinyatakan sembuh oleh dokter karena telah menyelesaikan pengobatan vaginitis.
                                        </p>
                                    </li>
                                     <li><b class="red-text">Vaginosis Bakteri</b>
                                        <p>
                                            Penyakit vaginitis yang disebabkan oleh bakteri, dan didiagnosis saat adanya pemeriksaan cairan pada vagina.
                                        </p>
                                    </li>
                                     <li><b class="red-text">Infeksi Ragi</b>
                                        <p>
                                            Penyakit vaginitis yang disebabkan oleh jamur, dan didiagnosis dengan pemeriksaan cairan vagina untuk mengetahui adanya sel ragi. Biasanya pengobatan akan berlangsung antara 1, 3 atau 7 hari namun kembali lagi sesuia dengan tingkat keparahannya. Infeksi ragi sulit untuk didiagnosis berdasarkan gejalanya saja, namun harus melakukan pemeriksaan laboratorium untuk diketahui adanya penyebabnya.
                                        </p>
                                    </li>
                                     <li><b class="red-text">Vaginitis Atrofi</b>
                                        <p>
                                            Untuk terapi estrogen dapat meningkatkan risiko adanya kanker payudara.
                                        </p>
                                    </li>
                                     <li><b class="red-text">Jenis Vaginitis Non Infeksius</b>
                                        <p>
                                            Hindari  semua produk organ seksual yang dapat menyebabkan segala bentuk iritasi.
                                        </p>
                                    </li>
                                </ul>
