                                Adapun Langkah-Langkah Untuk Mengurangi Kekambuhan Vaginitis Meliputi:
                            </p>

                            <div class="chitietbaiviet-lienquan container card">
                                <ul>
                                    <li>Hindari douching dan seringnya mandi</li>
                                    <li>Hindari semua produk kewanitaan apapun itu termasuk sabun pewangi atau semprotan pewangi (spray) dan yang lainnya yang berpotensi dapat menyebabkan munculnya iritasi pada vagina dan sekitarnya</li>
                                    <li>Hindari pakaian ketat dan pakaian dalam yang berbahan sintetis yang bisa menyebabkan kelembaban pada daerah vagina</li>
                                    <li>Melakukan hubungan seksual yang aman seperti tidak berganti-ganti pasangan atau hanya satu pasangan saja, atau dengan menggunakan pengaman atau kondom, atau tidak melakukan hubungan seksual sama sekali</li>
                                    <li>Menjaga kebersihan daerah organ seksual yang baik </li>
                                    <li>Hindari aktifitas yang berlebihan sehingga menimbulkan banyak keringat karena bisa menyebabkan kelembaban</li>
                                    <li>Selalu menjaga daerah vagina sesering mungkin</li>
                                    <li>Hindari melakukan aktivitas seksual sampai gejalanya dinyatakan sembuh oleh dokter</li>
                                    <li>Selalu bersihkan vagina dari depan ke belakang setelah buang air kecil (kencing atau BAK) dan setelah buang air besar (BAB)</li>
