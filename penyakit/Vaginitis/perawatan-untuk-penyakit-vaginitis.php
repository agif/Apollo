                                Perawatan Untuk Penyakit Vaginitis
                            </p>
                            <p>
                                Perawatan standar untuk penyakit vaginitis yang disebabkan oleh bakteri, meskipun perawatan vaginitis lain telah terbukti memiliki keefektifan yang setara, namun akan dikaitkan dengan tingkat kekambuhan yang lebih tinggi. Salah satu cara untuk menentukan infeksi disebabkan oleh persistensi infeksi awal atau infeksi ulang. Perawatan vaginitis tergantung pada apa yang menjadi penyebabnya.
                            </p>
                            <p>
                                Jika yang menyebabkan penyakit vaginitis itu adalah bakteri, dokter akan menyarankan perawatan apa yang harus dijalani setelah adanya pemeriksaan laboratorium dan telah adanya diagnosis. Penyebab infeksi dapat menentukan perawatan apa yang tepat. Jenis penyakit vaginitis sulit dibedakan berdasarkan gejala, sehingga pasien harus menjalani pemeriksaan pelvis(panggul). Pemeriksaan pelvis (panggul) terdiri dari pemeriksaan vagina dan serviks (leher rahim) sampai pengambilan sampel sekresi vagina di laboratorium. 
                            </p>

