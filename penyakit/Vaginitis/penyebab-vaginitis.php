                                Apa Yang Menjadi Penyebab Vaginitis?
                            </p>
                            <p>
                                Bakteri, ragi, virus, bahan kimia dalam krim atau semprotan atau bahkan pakaian yang bisa menyebabkan vaginitis. Kadang-kadang vaginitis terjadi dari organisme yang melewati pasangan seksual. Selain itu, lingkungan vagina dipengaruhi oleh sejumlah faktor yang berbeda termasuk kesehatan wanita, kebersihan pribadinya, obat-obatan, hormon (terutama estrogen), dan kesehatan pasangan seksualnya. Gangguan pada faktor-faktor ini bisa memicu vaginitis.
                            </p>
