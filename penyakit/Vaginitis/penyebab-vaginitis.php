                               Keputihan ini membuat jaringan tetap lembab dan sehat. Hal ini sangat normal dan warna keputihan yang berubah sepanjang siklus menstruasi. Vagina yang mengandung banyak organisme, seperti bakteri dan ragi atau bakteri yang bisa mengakibatkan vaginitis. Faktor-faktor yang dapat menyebabkan ketidakseimbangan antara lain:
