							Definisi Endometriosis
						</p>
						<p>
							Endometriosis adalah ketika jaringan yang melapisi bagian dalam rahim (endometrium) ditemukan atau tumbuh di luar rahim. Masalah yang cukup umum yang bisa sangat menyakitkan, pendarahan yang sangat berat dan bahkan membuat lebih sulit untuk hamil. Jika Anda memiliki endometriosis, jaringan ini cenderung tumbuh di ovarium (indung telur), saluran tuba dan bagian luar rahim serta organ perut.
						</p>
