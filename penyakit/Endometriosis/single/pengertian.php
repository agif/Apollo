							Bagaimana Cara Mendiagnosis Endometriosis?
						</p>
						<p>
							Bagi kebanyakan wanita yang memiliki diagnosis endometriosis harus dievaluasi mengenai riwayat kesehatan dan memerlukan pemeriksaan fisik termasuk pemeriksaan panggul (pelvic). Diagnosis endometriosis dengan melakukan laparoskopi, melakukan biopsi jaringan untuk mendiagnosisnya. Laparoskopi adalah prosedur operasi minor, tabung tipis yang dimasukkan ke dalam perut melalui sayatan kecil. Laparoskopi juga digunakan untuk menentukan lokasi, luas dan ukuran pertumbuhan endometrium.
