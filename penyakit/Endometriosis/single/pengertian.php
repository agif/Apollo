						<p class="font-weight-bold">
							Apa Saja Yang Menjadi Gejala Endometriosis?
						</p>

						<p>
							Rasa nyeri sebelum dan selama menstruasi adalah gejala endometriosis yang paling umum. Adanya kram yang sangat tidak menyenangkan bagi setiap orang. Gejala pada perut bagian bawah yang kronis atau adanya sakit pada punggung bagian bawah. Endometriosis juga bisa membuat seks pada vagina sangat tidak nyaman. Gejala lainnya seperti bercak diantara menstruasi dan memiliki menstruasi yang sangat deras serta infertilitas atau kemandulan. endometriosis terkadang membuat sakit saat kencing atau saat buang air besar (BAB). Gejala endometriosis yang kemungkinan setiap wanita dapat berbeda ketika mengalaminya meliputi:
						</p>

						<div class="chitietbaiviet-lienquan container card">
							<ul>
								<li>Rasa sakit terutama kram menstruasi yang berlebihan yang kemungkinan dirasakan di perut atau punggung bagian bawah</li>
								<li>Nyeri saat bersenggama</li>
								<li>Aliran menstruasi yang tidak normal dan berat</li>
								<li>Ketidaksuburan</li>
								<li>Nyeri saat buang air kecil selama menstruasi </li>
								<li>Nyeri saat buang air besar saat menstruasi</li>
								<li>Masalah lainnya seperti diare, konstipasi atau mual</li>
							</ul>
						</div>

						<p>
							Rasa sakit yang dialami wanita tidak harus berhubungan dengan tingkat keparahan penyakit. Beberapa wanita dengan endometriosis berat kemungkinan tidak mengalami rasa sakit, sementara yang lainnya dnegan bentuk penyakit yang lebih ringan kemungkinan akan memiliki rasa sakit atau gejala yang parah.
						</p>

