						</p>

						<p class="font-weight-bold">
							Apa Yang Menjadi Penyebab Endometriosis?
						</p>

						<p>
							Lapisan rahim (uterus) disebut endometrium. Beberapa sel dari endometrium keluar dari rahim ke daerah panggul (pelvic). Endometriosis yang lengket bisa bergabung dengan organ satu sama lain. Misalnya kandung kemih atau usus bisa menempel ke rahim. Endometriosis besar bisa terbentuk menjadi kista yang berdarah setiap bulannya atau istilahnya menstruasi.
						</p>

						<p>
							Selama menstruasi beberapa jaringan punggung naik melalui tuba fallopi ke perut dimana itu menempel dan tumbuh. Jaringan endometrium dapat berjalan dan tertanam melalui jalur darah sangat mirip dengan cara sel kanker ketika menyebar. Sel di lokasi manapun dapat berubah menajdi sel endometrium.
						</p>

						<p>
							Endometriosis juga dapat terjadi akibat transplantasi langsung di dinding perut setelah operasi caesar dan beberapa kemungkinan yang mempunyai faktor predisposisi genetik terhadap penyakit ini. Dimana endometriosis dapat terjadi:
						</p>

						<div class="chitietbaiviet-lienquan container card">
							<ul>
								<li>Ovarium (indung telur)</li>
								<li>Saluran tuba</li>
								<li>Ligamen yang mendukung rahim (ligamen uterosakral)</li>
								<li>Ruang antara rahim dan rektum (organ terakhir dari usus besar yang berakhir di anus)</li>
								<li>Ruang antara rahim dan kandung kemih</li>
								<li>Permukaan luar rahim</li>
								<li>Lapisan rongga pelvis (panggul)</li>
							</ul>
						</div>

						<p>
							Namun terkadang jaringan endometrium ditemukan di tempat lain seperti:
						</p>

						<div class="chitietbaiviet-lienquan container card">
							<ul>
								<li>Usus</li>
								<li>Rektum (organ terakhir dari usus besar yang berakhir di anus)</li>
								<li>Kandung kemih</li>
								<li>Vagina</li>
								<li>Leher rahim (serviks)</li>
								<li>Vulva (bagian organ seksual eksternal wanita yang merupakan area yang mengelilingi lubang kecil dan vagina)</li>
							</ul>
						</div>

