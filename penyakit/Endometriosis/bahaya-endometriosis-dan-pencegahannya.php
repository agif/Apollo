                        <p class="font-weight-bold">
                            Bagaimana Cara Mencegah Endometriosis
                        </p>

                        <p>
                            Terkadang Anda tidak bisa sepenuhnya dapat mencegah penyakit endometriosis  namun dapat secara signifikan mengurangi riisko terkena dampak dengan menurunkan kadar estrogen tubuh Anda. Hormon dapat membantu penebalan lapisan didalam rahim selama siklus haid. Terjadi ketika endometriosis biasanya muncul. Dan beberapa cara untuk menurunkan kadar estrogen tubuh meliputi:
                        </p>

                        <div class="chitietbaiviet-lienquan container card">
                            <ul>
                                <li>Berolahraga secara teratur selama lebih dari 4 jam dalam 1 minggu</li>
                                <li>Makan makanan sehat dan kaya akan makanan yang utuh</li>
                                <li>Menghindari alkohol</li>
                                <li>Menghindari minuman berkafein dalam jumlah besar</li>
                                <li>Menghindari makanan dengan kandungan kedelai tinggi</li>
                                <li>Mengurangi Aparan terhadap xenoestrogen di lingkungan Anda</li>
                            </ul>
