                        <p class="font-weight-bold">
                            Bahaya Untuk Penyakit Endometriosis
                        </p>
                        <p>
                            Ada beberapa hal lain yang bisa meningkatkan risiko keguguran. Saat berusia 35 tahun atau lebih merupakan salah satu risiko yang mempengaruhi laki-laki dan perempuan. Khususnya untuk perempuan, risiko tambahannya meliputi:
                        </p>

                        <div class="chitietbaiviet-lienquan container card">
                            <ul>
                                <li>Keguguran sebelumnya</li>
                                <li>Kegemukan atau obesitas</li>
                                <li>Sindrom ovarium polikistik (gangguan hormonal yang menyebabkan pembesaran ovarium dengan kista kecil di tepi luar)</li>
                                <li>Infeksi virus atau bakteri tertentu selama kehamilan</li>
                                <li>Gangguan pembekuan darah</li>
                                <li>Kelainan pada struktur rahim</li>
                                <li>Aparan obat-obatan atau bahan kimia tertentu selama kehamilan</li>
                                <li>Merokok atau menggunakan alkohol atau kokain selama kehamilan</li>
                                <li>Asupan kafein berlebihan selama kehamilan</li>
                            </ul>
                        </div>

                        <p>
                            Sebagian besar keguguran terjadi karena sel telur yang dibuahi di rahim tidak berkembang secara normal, bukan karena apapun. Keguguran tidak disebabkan oleh olahraga, stress atau apapun. 
                        </p>
                       
