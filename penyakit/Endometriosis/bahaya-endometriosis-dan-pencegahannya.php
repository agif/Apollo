                        <p class="font-weight-bold">
                            Faktor Risiko Dari Endometriosis
                        </p>

                        <p>
                            Faktor risikonya adalah segala sesuatu yang mempengaruhi kesempatan terkena penyakit seperti kanker. Kanker yang berbeda memiliki faktor risiko yang berbeda. Beberapa faktor risiko seperti merokok namun bisa diubah. Faktor lainnya adalah usia seseorang atau dari riwayat keluarga dan itu tidak dapat diubah. 
                        </p>
                        <p>
                            Meskipun faktor-faktor tertentu dapat meningkatkan risiko seorang wanita terkena kanker endometrium namun tidak selalu menyebabkan penyakit ini. Kebanyakan wanita yang memiliki faktor risiko tidak pernah mengalami kanker endometrium. Beberapa wanita dengan kanker endometrium tidak memiliki faktor risiko yang diketahui. 
                        </p>
                        <p>
                            Beberapa faktor yang mempengaruhi risiko pengembangan kanker endometrium diantaranya:
                        </p>

                        <div class="chitietbaiviet-lienquan container card">
                            <ul>
                                <li>Hal-hal yang dapat mempengaruhi kadar hormon seperti mengonsumsi estrogen setelah menopause, pil KB, jumlah siklus haid, kehamilan, obesitas, tumor ovarium tertentu dan sindrom ovarium polikistik.</li>
                                <li>Penggunaan alat kontrasepsi</li>
                                <li>Usia</li>
                                <li>Diet dan olahraga</li>
                                <li>Diabetes (kencing manis)</li>
                                <li>Riwayat keluarga</li>
                                <li>Pernah dididagnosis menderita kanker payudara atau ovarium dimasa lalu</li>
                                <li>Telah didiagnosis dengan hiperplasia endoemtrium dimasa lalu</li>
                                <li>Perawatan dengan terapi radiasi ke panggul (pelvic) untuk Merawat kanker lain</li>
                            </ul>
                        </div>
                        
