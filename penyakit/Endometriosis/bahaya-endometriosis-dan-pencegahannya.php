                            Endometriosis Dan Risiko Kelahiran Prematur
                        </p>

                        <p>
                            Jika Anda memiliki kista ovarium (endometrioma) dan menjadi hamil dengan menggunakan teknologi reproduksi terbaru, dapat berisiko bayi yang lebih besar. Kelahiran dini sebelum 40 minggu (kelahiran prematur). Wanita yang didiagnosis dengan endometriosis memiliki risiko komplikasi yang lebih tinggi saat kelahiran, kelahiran prematur dan operasi caesar.
