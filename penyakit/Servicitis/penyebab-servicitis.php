                                    <li>Obat-obatan</li>
                                    <li>Stress dan kecemasan</li>
                                    <li>Pubertas atau menopause</li>
                                    <li>Kontrol kelahiran hormonal seperti pil kontrasepsi atau kontrasepsi darurat</li>
                                </ul>
