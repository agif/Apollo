                        <p class="font-weight-bold">
                            Apa Yang Menjadi Bahaya Pada Servicitis?
                        </p>
                        <p>
                            Servicitis dapat berkembang selama bertahun-tahun dengan keputihan, tandanya dengan demam, gangguan menstruasi atau gangguan hubungan seksual. Dan terkadang servicitis dapat dipersulit oleh radang organ pelvis (panggul):
                        </p>

                        <div class="chitietbaiviet-lienquan container card">
                            <ul>
                                <li>Peradangan ligamen yang mendukung rahim dan organ panggul yang bisa menyebabkan sakit perut, dismenore (nyeri haid yang seringkali dialami oleh wanita pada saat menjelang dan selama menstruasi), dispareunia (nyeri di vagina atau pinggul yang dialami selama hubungan seksual) dan menorrhagia (perdarahan berlebih yang tidak biasa pada menstruasi normal)</li>
                                <li>Salpingitis (radang tuba falopi) yang dapat menyebabkan infertilitas atau kemandulan, obstruksi tuba sekunder akibat proses inflamasi</li>
                                <li>Infeksi saluran kemih kronis</li>
                            </ul>
                        </div>

                        <p>
                            Peradangan kronis pada serviks (leher rahim) yang dapat menyebabkan stenosis (penyempitan kanal tulang belakang) serviks (leher rahim) yang bisa mengakibatkan ketidaksuburan. Iritasi kronis dapat menyebabkan kanker serviks (leher rahim). Servicitis kronis bukan penyebab kanker. Servicitis disebabkan oleh gonore (kencing nanah) atau klamidia yang dapat berpindah ke lapisan rahim dan saluran tuba, yang dapat menyebabkan penyakit radang panggul (PID). Penyakit radang panggul (PID) menyebabkan nyeri panggul tambahan, pelepasan dan demam. Penyakit radang panggul (PID) yang tidak diobati dapat menyebabkan masalah kesuburan.
                        </p>
                       
