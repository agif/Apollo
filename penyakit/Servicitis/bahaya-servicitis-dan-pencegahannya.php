                        <p class="font-weight-bold">
                            Apa Saja Yang Meningkatkan Risiko Terkena Servicitis?
                        </p>

                        <div class="chitietbaiviet-lienquan container card">
                            <ul>
                                <li><font class="red-text">Usia</font>
                                    <p>
                                        Risiko untuk servicitis dapat meningkat seiring bertambahnya usia dan tingkat estrogen menurun
                                    </p>
                                </li>
                                <li><font class="red-text">Douching</font>
                                    <p>
                                        Ini bisa mengganggu keseimbangan bakteri yang normal yang hidup di vagina dan dapat menyebabkan infeksi vagina
                                    </p>
                                </li>
                                <li><font class="red-text">Beberapa Pasangan Seks</font>
                                    <p>
                                        Mencakup memiliki lebih dari 1 pasangan seks pada 1 waktu atau sering berganti pasangan. Termasuk memiliki pasangan seksual yang berhubungan seks dengan orang lain
                                    </p>
                                </li>
                                <li><font class="red-text">Infeksi Menular Seksual (IMS) Lainnya</font>
                                    <p>
                                        Kemungkinan berisiko lebih tinggi jika pernah mengalami infeksi menular seksual (IMS) dimasa lalu
                                    </p>
                                </li>
                                <li><font class="red-text">Seks Tanpa Pelindung</font>
                                    <p>
                                        Tidak menggunakan pengaman atau kondom saat berhubungan seksual dapat meningkatkan risiko infeksi menular seksual (IMS)
                                    </p>
                                </li>
                            </ul>
