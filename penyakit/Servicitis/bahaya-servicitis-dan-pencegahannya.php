                        <p class="font-weight-bold">
                            Bagaimana Cara Mencegah Servicitis?
                        </p>

                        <div class="chitietbaiviet-lienquan container card">
                            <ul>
                                <li><font class="red-text">Hindari produk yang dapat menyebabkan iritasi</font>
                                    <p>
                                        Jangan melakukan douche kecuali oleh dokter. Jangan menggunakan spermisida (alat kontrasepsi yang mengandung bahan kimia yang digunakan untuk membunuh sperma) jika menimbulkan gejala
                                    </p>
                                </li>
                                <li><font class="red-text">Gunakan Pengaman Atau Kondom </font>
                                    <p>
                                        Gunakan pengaman atau kondom lateks setiap kali berhubungan sesk. Jika alergi terhadap lateks maka gunakan pengaman atau kondom non lateks
                                    </p>
                                </li>
                                <li><font class="red-text">Batasi pasangan seksual</font>
                                    <p>
                                        Risiko terkena infeksi menular seksual (IMS) menurun jika memiliki lebih sedikit pasangan seksual. Jangan berhubungan seks dengan seseorang yang telah atau sedang dalam perawatan infeksi menular seksual (IMS)
                                    </p>
                                </li>
                                <li><font class="red-text">Bicarakan dengan pasangan seksual Anda</font>
                                    <p>
                                        Jika Anda memiliki infeksi menular seksual (IMS) segera beritahu pasangan seksual terakhir Anda. Agar segera dilakukan perawatan untuk membantu menghentikan penyebaran infeksi.
                                    </p>
                                </li>
                            </ul>
                        </div>

                        <p>
                            Ada berbagai cara untuk mengurangi risiko tersebarnya servicitis termasuk hubungan seksual yang aman. Dan disarankan untuk menggunakan pengaman atau kondom selama aktifitas seksual karena untuk mengurangi kemungkinan penyakit menular seksual (PMS) dan masalah lainnya. Douching dapat menyebabkan masalah seperti servicitis sehingga sangat penting untuk menghindari penggunaan hal-hal lain.
                        </p>

