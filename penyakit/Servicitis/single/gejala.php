                            <p class="font-weight-bold">
                                Gejala Servicitis Bisa Mencakup:
                            </p>

                            <p>
                                Gejala servicitis yang paling umum adalah keputihan dan pendarahan pada vagina antar periode menstruasi atau setelah persalinan. Beberapa wanita mengalami dispareunia (suatu nyeri pada genital yang selalu menetap atau sering terjadi berulang-ulang ketika berhubungan intim, dari segi fisiologis tentu akan menyebabkan tekanan pada penderita secara personal), vulva (bagian organ seksual eksternal wanita yang merupakan area yang memiliki lubang kencing dan vagina) atau iritasi vagina dan disuria (buang air kecil atau kencing yang terasa sakit).
                            </p>
                            <p>
                                Jika uretra (tabung urine) juga terinfeksi, kemungkinan akan merasa seperti terbakar saat buang air kecil (kencing) dan akan buang air kecil (kencing) yang lebih sering atau urgensi. Gejala servicitis dapat menyebar ke rahim, saluran tuba atau ovarium (indung telur) yang merupakan kondisi yang disebut pelvic inflammatory disease (PID) atau biasa disebut penyakit radang panggul. Ditandai dengan adanya sakit perut dan demam.
                            </p>
                            <p>
                                Namun, kemungkinan servicitis tidak menimbulkan gejala apapun. Jika terjadi adanya gejala, yang paling umum adalah adanya debit yang tidak biasa (kadang-kadang berwarna kuning atau hijau) dari vagina dan terjadi pendarahan pada vagina diantara priode menstruasi atau setelah melakukan hubungan seksual. beberapa dari wanita sering mengalami nyeri saat berhubungan seksual, nyeri saat buang air kecil (kencing), atau nyeri keduanya secara bersamaan. Sedangkan area disekitar lubang vagina kemungkinan akan berwarna merah.
                            </p>
                            <p>
                                Pada umumnya setiap wanita akan memiliki gejala yang lain tergantung pada apa yang menyebabkan penyakit servicitis itu sendiri. Misalnya, jika penyebabnya adalah penyakit radang panggul atau infeksi herpes simpleks, kebanyakan wanita kemungkinan akan mengalami demam dan nyeri pada bagian bawah perut. Serviks (leher rahim) bisa menjadi sangat meradang jika terjadi servicitis. Dalam beberapa kasus, bisa menimbulkan luka terbuka. Ini bisa termasuk gejala servicitis yang sangat parah. Sangat penting untuk melakukan pemeriksaan ketika gejala muncul atau pernah menderita klamidia atau penyakit menular seksual (PMS) lainnya dimasa lalu.
                            </p>

