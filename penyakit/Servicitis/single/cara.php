                            <p class="font-weight-bold">
                                Poin Utama Dari Pengobatan Servicitis
                            </p>
                           
                            <div class="chitietbaiviet-lienquan container card">
                                <ul>
                                    <li>Servicitis akut biasanya disebabkan oleh penyakit menular seksual (PMS) dan dapat menandakan penyakit radang panggul (PID)</li>
                                    <li>Infeksi asimtomatik (suatu penyakit ketika pasien tidak menyadari gejala apapun)</li>
                                    <li>Melakukan tes untuk infeksi klamidia, gonore, vaginosis bakteri dan trikomoniasis</li>
                                </ul>
                            </div>

                            <p>
                                Untuk penyakit servicitis yang disebabkan oleh reaksi alergi terhadap semua produk yang berhubungan dengan kewanitaan dan spermisida. Jika Anda menderita servicitis yang disebabkan oleh infeksi menular seksual (IMS) dan pasangan Anda juga memerlukan perawatan atau pengobatan untuk menghindari adanya infeksi yang lain seperti gonore, klamidia atau infeksi bakteri termasuk bakteri vaginosis.
                            </p>
                            <p>
                                Jika Anda memiliki herpes genital, dokter akan memberikan pengobatan awal untuk mengurangi jumlah gejala servicitis yang diderita. Herpes adalah kondisi kronis yang bisa ditularkan ke pasangan seksual Anda kapan saja. Maka harus direkomendasikan untuk melakukan pengujian atau tes laboratorium secara ulang untuk penyakit servicitis yang disebabkan oleh gonore atau klamidia. Untuk menghindari penyebaran infeksi bakteri ke pasangan Anda, dan hindri untuk tidak melakukan hubungan seksual sampai pengobatan dinyatakan benar-benar sembuh.
