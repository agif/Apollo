                            <p class="font-weight-bold">
                                Pemilihan Pengobatan Untuk Servicitis
                            </p>

                            <p>
                                Tidak ada pengobatan servicitis yang standar. Namun ada beberapa faktor penunjang untuk pengobatan servicitis, diantaranya:
                            </p>

                            <div class="chitietbaiviet-lienquan container card">
                                <ul>
                                    <li>Memeriksakan kesehatan secara menyeluruh</li>
                                    <li>Riwayat kesehatan atau riwayat hubungan seksual</li>
                                    <li>Tingkat keparaha suatu gejala atau suatu penyakit</li>
                                    <li>Tingkat peradangan yang terjadi</li>
                                </ul>
                            </div>

                            <p>
                                Pengobatan yang umum untuk membunuh infeksi dan mewaspadai terutama setelah melahirkan. Penggunaan cryosurgery (penggunaan suhu ekstrem untuk memusnahkan jaringan yang sakit)atau perak nitrat pada kasus yang sudah sangat parah bila terjadi kerusakan pada sel serviks (leher rahim). Penggunaan cyosurgery melibatkan suhu beku untuk membekukan sel abnormal di serviks (leher rahim), yang kemudian dihancurkan. Silver nitrat juga bisa menghancurkan sel abnormal.
                            </p>
                            <p>
                                Ketika pengobatan servicitis yang telah diketahui penyebabnya, jika sevicitis tidak segera diobati secara bertahun-tahun mkaa dapat menyebakan hubungan seksual yang menyakitkan dan gejala yang semakin memburuk. Pengobatan servicitis sesuai dengan jenis infeksi yang ada. Jika adanya faktor infeksi menular seksual (IMS) seperti hubungan seksual tanpa pengaman atau kondom dengan pasangan baru atau pasangan multiple dan pemeriksaan fisik menunjukkan Anda menderita servicitis, maka harus segera ditindak lanjuti sebelum penyakitnya meradang.
                            </p>

