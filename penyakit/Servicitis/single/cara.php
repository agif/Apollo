                            <p class="font-weight-bold">
                                Pengobatan Apa Untuk Servicitis?
                            </p>
                            <p>
                                Pengobatan servicitis tergantung pada apa yang menjadi penyebabnya, dan salah satunya adalah:
                            </p>

                            <div class="chitietbaiviet-lienquan container card">
                                <ul>
                                    <li>Penyebab infeksi serviks (leher rahim) karena gonore dan klamidia</li>
                                    <li>Herpese genital pada umumnya</li>
                                    <li>Bakteri dan virus servicitis dapat menyebabkan tanda dan gejala yang parah</li>
                                </ul>
                            </div>

                            <p>
                                Servicitis yang disebabkan oleh reaksi alergi terhadap produk seperti spermisida (alat kontrasepsi yang mengandung bahan kimia yang digunakan untuk membunuh sperma) atau produk kebersihan wanita. Namun jika Anda menderita srevicitis yang disebabkan oleh infeksi menular seksual (IMS), maka pasangan seksual Anda juga harus melakukan pengobatan. Infeksi menular seksual (IMS) seperti gonore, klamidia, infeksi bakteri dan termasuk bakteri vaginosis.
                            </p>
                            <p>
                                Pengobatan servicitis yang disebabkan oleh herpes genital dapat membantu mengurangi jumlah waktu ketika Anda menderita gejala servicitis. Herpes adalah kondisi kronis yang bisa ditularkan ke pasangan seksual Anda kapan saja. Servicitis yang disebabakan oleh gonore dan klamidia akan direkomendasikan untuk melakukan tes ulang. Untuk menghindari penyebaran infeksi bakteri ke pasangan, dan hindari untuk melakukan hubungan seksual sampai pengobatannya selesai.
                            </p>

