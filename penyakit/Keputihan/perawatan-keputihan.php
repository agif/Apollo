                            Keputihan bisa dialami oleh semua wanita yang kurang mampu menjaga kebersihan kulit atau pakaiannya, sehingga menyebabkan pertumbuhan jamur yang bisa menyebabkan keputihan. Selain itu, penyakit ini juga bisa disebabkan oleh peradangan yang trejadi pada tabung atau perubahan hormon. Keputihan sebenarnya adalah hal yang wajar terjadi karena perubahan hormonal terutama selama kehamilan.
