                        <p>
                            Keputihan adalah penyakit yang disebabkan oleh infeksi atau pembengkakan alat kelamin dan umumnya diderita oleh wanita. Hal ini disebabkan infeksi oleh organisme seperti bakteri, virus dan juga bisa disebabkan karena pengaruh zat kimia seperti cairan, krim digunakan di area organ intim. Dalam beberapa kasus ditemukan bahwa keputihan ini bisa disebabkan oleh organisme yang ditularkan melalui pasangan seksual.               
                        </p>
                        <p>
                            Klinik yang menangani permasalahan penyakit kulit dan kelamin di Jakarta Indonesia adalah Klinik Sentosa. Selain itu menangani penyakit menular seksual (PMS), infeksi menular seksual (IMS), Andrologi dan Ginekologi. Bergantung pada spesiesnya, keputihan kemungkinan bersifat fisiologis (dalam keadaan normal) tapi juga bisa bersifat patologis (karena sakit).
                        </p>
                        <p>
                            Keputihan fisiologis biasanya terjadi pada masa subur, baik sebelum maupun sesudah menstruasi. Terkadang saat adanya lendir yang berlebihan, itu biasa saja. Dan biasanya tidak gatal dan tidak berbau. Keputihan seperti dapat ditemukan pada bayi baru lahir fisiologis sampai usia sekitar sepuluh hari, wanita dewasa saat ia dirangsang sebelumnya dan selama persendian (koitus), saat ovulasi pada wanita dengan neurosis kronis yang berpenyakit, dan wanita dengan ektropian porsionis uteri. Maka dari itu, wanita harus menjaga sistem reproduksinya dengan baik karena sangat penting untuk kesuburan setiap orang.
                        </p>
                        <p>
                            Proses pelayanan di Klinik Sentosa yang baik serta banyak pasien yang datang untuk berobat dan hasilnya memuaskan. Maka dari itu Klinik Sentosa sangat mengutamakan kepuasan pasien. Sehingga dipercaya oleh semua kalangan masyarakat dalam menangani permasalahan penyakit kelamin.
                        </p>
                        <p>
                            Jangan pernah menunda-nunda dalam melakukan perawatan penyakit kelamin. Karena sebelum penyakitnya semakin parah dan Perawatannya semakin mahal. Ada baiknya segera konsultasikan diri Anda ketika Anda sudah mengalami adanya gejala penyakit kelamin tersebut. Setelah adanya konsultasi, lalu dilakukan pemeriksaan yang akan dilakukan tes yang akan dilakukan di laboratorium untuk diketahui penyebab pastinya.
                        </p>
                        <p>
                            Ketika adanya diagnosis, dokter akan menyarankan perawatan selanjutnya tahap apa saja yang harus dijalani sesuai dengan tingkat keparahan penyakitnya. Disarankan untuk melakan perawatan sampai selesai, karena jika tidak melakukan perawatan sampai selesai maka akan resisten. Suatu saat penyakitnya akan mengalami kekambuhan, atau infeksi berulang.
                        </p>

