						<p>
							Jika keasaman pada vagina berubah, kuman lain akan tumbuh dengan mudah akibat infeksi yang bisa terajdi yang akhirnya menyebabkan keputihan, bau, gatal dan menimbulkan ketidaknyamanan. Begitu seorang wanita melakukan hubungan seksual, maka ia membuka semua kuman yang keluar dari luar. Keputihan juga bisa didapat atau ditularkan dari beberapa kuman yang dapat menyebabkan adanya penyakit kelamin yang kemungkinan dibawa oleh pasangan seks.
						</p>
						<p>
							Jadi lebih baik tidak melakukan hubungan seksual sampai menikah. Karena bisanya para wanita yang belum pernah berhubungan seks jarang terkena keputihan patologis. Pada setiap wanita yang belum pernah melakukan hubungan seksual juga bisa terjadi keputihan. Keputihan bagaimanapun dapat menyebabkan kemungkinan yang menggunakan pakaian secara bersama-sama, handuk bersama, kebersihan area vagina yang kurang higienis dan juga cara yang salah ketika membersihkan anal (sebaiknya dari depan vagina ke belakang anus).
