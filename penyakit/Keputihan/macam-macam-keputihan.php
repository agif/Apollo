							Berbagai Macam Keputihan
						</p>
						<p>
							Bergantung pada spesiesnya. Keputihan yang bersifat fisiologis (dalam keadaan normal) tapi juga bisa bersifat patologis (karena sakit). Keputihan fisiologis terjadi pada masa subur, baik itu sebelum maupun sesudah menstruasi. dan terkadang saat adanya lendir yang berlebihan itu biasa saja. Dan biasanya tidak gatal dan tidak berbau. Keputihan seperti ini ditemukan pada bayi baru lahir fisiologis sampai usia sekitar 10 hari, wanita dewasa yang dirangsang sebelumnya dan selama atau saat ovulasi pada wanita dengan nourosis kronis yang berpenyakit dan pada wanita yang menderita ektropion porsionis uteri.   
						</p>
						<p>
							Keputihan patologis terjadi karena infeksi di vagina dan adanya benda asing ke dalam vagina atau gangguan gormonal akibat menopause dan kelainan bawaan alat kelamin wanita, infeksi ini bisa terjadi akibat bakteri, jamur dan protozoa. Karakteristik keputihan pada vagina patologis tidak seperti warna lendir. Keputihan patologis biasanya berwarna krem atau hijau kekuningan atau bahkan bercampur darah. Keputihan salah satu  penyakit pada wanita, keputihan patologis ini akan terasa gatal didaerah vagiba dan berbau lendir serta menyebabkan ketidaknyamanan.
						</p>

