							 Perbedaan Keputihan Fisiologis Dan Patologis
						</p>

						<p>
							Perbedaannya adalah cairan fisiologis yang terkadang merupakan epitel lendir yang banyak mengandung leukosit. Dan sementara patologis ada lebih banyak leukosit. Didalam vagina bukan tempat yang steril. Berbagai jenis kuman dapat tergantung. Jika tidak seimbang maka akan berakibat pada pertumbuhan bakteri, jamur dan kuman lainnya.
