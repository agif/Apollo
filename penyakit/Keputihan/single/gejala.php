							Gejala keputihan dapat bervariasi intensitasnya dari wanita ke wanita. Beberapa wanita kemungkinan akan mengalami beberapa gejala secara bersamaan, sementara yang lain kemungkinan akan relatif asimtomatik. Gejala keputihan seperti iritabilitas yang parah dan adanya bercak hitam di mata umumnya terjadi pada kasus keputihan yang sangat kronis.
