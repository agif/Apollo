							Apa Yang Menjadi Penyebab Keputihan ?
						</p>
						<p>
							Keputihan patologis dapat terjadi karena alat kelamin luar.  Ada banyak bakteri yang masuk melalui hubungan seksual atau saat haid. Keputihan-keputihan yang berlebihan bisa terjadi pada kontak dengan benda asing misalnya kadang-kadang penampilan atau bentuknya akan lebih putih karena spiral rahim. Keputihan uterus  (reproduksi yang utama pada manusia) juga terjadi dengan pembengkakan mukosa (lapisan kulit dalam yang tertutup pada epitelium atau jaringan biologis yang terdiri dari banyak sel yang membentuk rongga-rongga terstruktur dan terlibat dalam proses absorpsi dan proses sekresi) rahim berbagai etiologi atau penyebab dan neoplasma (pertumbuhan abnornal namun bukan kanker yang mungkin terjadi di berbagai bagian tubuh) rahim (misalnya polip, kanker rahim, dan lain-lain). Perubahannya juga bisa terjadi dengan adanya influenza dan diabetes.     
						</p>
						<p>
							Keputihan fisiologis dan umumnya terjadi pada wanita sebelum pubertas selama awal kehamilan, atau sebelum mentsruasi atau pendarahan dan selama sering melakukan hubungan seksual. Penyebab keputihan patologis adalah nutrisi yang tidak tepat atau kesehatan yang memburuk dan kelemahan setelah penyakit seperti demam virus. Penyebab lain yang menjadi perhatian adalah disfungsi saluran genital, pembengkakan dan infeksi pada saluran vagina.
						</p>
						<p>
							Ketidakseimbangan yang hormonal, kebersihan alat kelamin yang buruk, luka yang disebabkan oleh goresan yang diikuti adanya rasa gatal juga bisa menjadi salah satu penyebab keputihan. Dan beberapa masalah kesehatan seperti sembelit (kondisi dimana seseorang buang air besar atau BAB kurang dari 3 kali dalam seminggu), anemia  (kurang darah atau kondisi ketika darah tidak memiliki sel darah merah sehat yang cukup), menorrhagia (pendarahan berlebih yang tidak biasa pada menstruasi normal), gangguan pencernaan dan diabetes (kencing manis atau sejumlah penyakit yang mengakibatkan terlalu banyak kadar gula dalam darah atau glukosa yang tinggi)juga bisa berakibat yang sama. Pada wanita yang aktif secara seksual, seperti gonore dan penyakit menular seksual (PMS) lainnya kemungkinan menjadi penyebabnya.
						</p>

