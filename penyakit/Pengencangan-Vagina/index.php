    function debounce(wait, bool, func) {
        var timeout;
        return function() {
            var context = this,
                args = arguments;
            var executeFunction = function() {
                func.apply(context, args);
            };
            if (bool == false) {
                clearTimeout(timeout);
                timeout = setTimeout(executeFunction, wait);
            };
            if (bool == true) {
                func.apply(context, args);
            };
        }
    };
    var test = false;
    $(window).scroll(debounce(1000, true, function() {
