								Penyebab Umum Dari Erosi Serviks 
						</p>

						<p>
								Penyebab umum dari erosi serviks adalah ketidakseimbangan hormon pada wanita. Hormon estrogen ini kebanyakan disalahkan untuk kondisi ini. Namun, akan ada yang namanya trauma, karena beberapa bahan kimia dan infeksi di kanal serviks sebagai penyebab erosi atau abrasi vagina. Ektopi serviks adalah umum selama pubertas, selama kehamilan dan pada wanita yang berada pada kontrol pil KB. Dimana hormon estrogen banyak terpengaruh dan berfluktuasi.
						</p>
						<p>
								Penyebab masalah serviks (leher rahim) banyak dan bervariasi. Kemungkinan berkaitan dengan penyakit menular seksual (PMS) seperti gonore, sifilis atau klamidia. Dalam beberapa kasus persalinan yang sulit dapat menyebabkan infeksi. Yang menyebabkan erosi serviks tidak selalu jelas, gesekan hubungan intim nampaknya merupakan faktor sekaligus pil kontrasepsi.
						</p>
						<p>
								Selain itu, diantara penyebab erosi berbagai infeksi seksual (khususnya mycoplasmosis, trikomoniasis atau klamidia), trauma mekanik pada selaput lendir serviks, serta gangguan hormonal pada tubuh wanita. Erosi serviks (leher rahim) sampai saat ini adalah penyakit paling umum pada bidang seksual wanita dan dapat menyebabkan berbagai komplikasi pada pengobatan dini (infeksi pada sistem urogenital, infertilitas atau kemandulan dan aborsi).
