							Apa Yang Menyebabkan Erosi Serviks?
						</p>
						<p>
								Infeksi klamidia disebabkan oleh mikroorganisme parasit yang hidup. Seseorang biasanya tidak mengalami gejala apapun selama beberapa bulan ketika terjadi, gejalanya termasuk nyeri panggul (pelvic), demam, pendarahan, keputihan dan buang air kecil (kencing) yang tidak nyaman. Pada pria, infeksi biasanya ditandai dengan buang air kecil (kencing) yang menyakitkan yang  keluar dari penis.  
						</p>
						<p>
								Klamidia perlu dilakukan pengobatan yang tepat sebelum menyebar ke tuba falopi atau rahim. Seorang wanita perlu menjalani pemeriksaan ginekologi secara teratur untuk mencegah penyakit lainnya yang kemungkinan akan muncul. Pemeriksaan tersebut meliputi pemeriksaan pap smear yang mendeteksi adanya sel abnormal pada permukaan serviks (leher rahim). 
						</p>
						<p>
								Langkah kedua dari pemeriksaan ginekologi terdiri dari pemeriksaan tahunan organ reproduksi dan rektum (tempat penyimpanan sementara feses) untuk memeriksa adanya nyeri yang tertekan. Gonore biasanya tidak melibatkan gejala apapun sampai seseorang mengalami keputihan yang tidak biasa, nyeri pelvic (panggul), buang air kecil yang menyakitkan, demam dan pendarahan yang tidak biasa. 
						</p>
						<p>
								Jika penyakitnya tidak diobati tepat waktu, gonore dapat menyebabkan masalah kesuburan, masalah kulit, keracunan darah, infeksi jantung, artritis ( radang sendi atau peradangan pada salah satu atau lebih , menyebabkan nyeri dan kekauan yang dapat memburuk seiring usia) dan infeksi otak. Reinfeksi terkadang bisa terjadi, jadi pasien harus sangat berhati-hati.
						</p>
						<p>
								Trichomonas dipicu oleh organisme mikroskopik sederhana dan ditandai dengan buih berbusa dari vagina. Kadang-kadang juga termasuk kemerahan, nyeri, gatal dan bau yang tidak menyenangkan. Trichomonas jarang ada gejala pada pria namun dalam kasus yang jarang terjadi kemungkinan pasien merasa iritasi atau kemungkinan seperti sensasi terbakar saat buang air kecil (kencing).
						</p>

