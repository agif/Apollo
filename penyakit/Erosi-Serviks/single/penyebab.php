								Kondisi Munculnya Penyebab Erosi Serviks
						</p>
						<p>
								Penyebab utama erosi serviks adalah proses inflamasi asal menular. Epitel permukaan serviks (leher rahim) dapat menyebabkan mikroorganisme yang termasuk dalam infeksi urogenital, ureaplasma, mikoplasma, trikomoniasis, klamidia, virus herpes genital. Dalam beberapa kasus penyebab erosi serviks (leher rahim) kemungkinan merupakan pelanggaran terhadap sistem endoktrin, terutama fungsi kelenjar yang bertanggung jawab atas produksi hormon seks.
						</p>
