						<p class="font-weight-bold">
							Bagaimana Cara Mendiagnosis Erosi Serviks?
						</p>

						<p>
							Erosi serviks dapat didiagnosis hanya pada pemeriksaan ginekologi. Hal seperti ini divisualisasikan dengan spekulum selama kolposkopi. Kolposkopi adalah ketika serviks uterus diperiksa dengan alat yang dimasukkan ke dalam vagina. Dengan demikian erosi serviks merupakan pertanda penyakit yang berbahaya bagi wanita. Termasuk kanker serviks yang didiagnosis pada pemeriksaan sitologi. Diagnosis secara umum meliputi:
						</p>

						<div class="chitietbaiviet-lienquan container card">
							<ul>
								<li>Pemeriksaan ginekologi secara umum serviks rahim</li>
								<li>Pengujian infeksi menular seksual (IMS) dan mengenai Human Papilloma Virus (HPV)</li>
								<li>Sampel serviks uterus, saat epitel menjadi berwarna coklat</li>
								<li>Kolposkopi, pemeriksaan rahim dilakukan dengan mikroskop</li>
								<li>Histeroskopi adalah pemeriksaan kanal serviks dengan histeroskopi</li>
								<li>Pemeriksaan sitologi smear yang diambil dari serviks rahim dan kanal serviks. Digunakan untuk menentukan struktur sel dari lapisan kanal serviks</li>
								<li>Biopsi adalah saat sampel jaringan daimbil untuk pemeriksaan mikroskopis</li>
								<li>Pemeriksaan panggul</li>
