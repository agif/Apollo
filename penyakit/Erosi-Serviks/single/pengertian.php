							Bagaimana Cara Mengobati Erosi Serviks?
						</p>
						<p>
							Jika erosi serviks tidak memiliki gejala namun telah ditemukan saat pemeriksaan rutin. Jika gejala muncul, perawatan aktif sangat diperlukan. Tujuan pengobatan erosi serviks adalah menghancurkan sel kolumnar sehingga sel skuamosa normal dapat tumbuh.
						</p>

						<div class="chitietbaiviet-lienquan container card">
							<ul>
								<li><big class="font-weight-bold">Elektrokauter</big>
									<p>
										Sel-sel dibakar dengan menggunakan panas yang dihasilkan oleh arus listrik
									</p>
								</li>
								<li><big class="font-weight-bold">Diathermy</big>
									<p>
										Suhu tinggi diaplikasikan pada area erosi serviks melalui gelombang pendek atau arus listrik gelombang mikro dengan ultrasonografi untuk menghasilkan panas yang dalam sehingga sel-sel nya rusak
									</p>
								</li>
								<li><big class="font-weight-bold">Cryocautery</big>
									<p>
										Suhu yang sangat dingin ekstrem yang dihasilkan oleh gas yang digunakan untuk menghancurkan sel
									</p>
								</li>
							</ul>
