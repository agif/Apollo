						<p class="font-weight-bold">
							Apa Yang Menyebabkan Erosi Serviks?
						</p>

						<p>
							<big class="red-text">Hormon Estrogen:</big> erosi serviks yang diyakini sebagai respons terhadap tingginya kadar estrogen yang beredar di tubuh. Tingkat estrogen kemungkinan tinggi pada waktu yang berbeda dalam kehidupan seorang wanita.
						</p>

						<div class="chitietbaiviet-lienquan container card">
							<ul>
								<li>
									<p>Erosi serviks adalah tanda yang sangat umum selama kehamilan dan diyakini karena tingginya tingkat kadar estrogen di tubuh. Dapat menyebabkan pendarahan ringan selama kehamilan, biasnaya saat berhubungan seksual saat penis menyentuh serviks (leher rahim).</p>
								</li>
								<li>
									<p>Semua pil KB mengandung hormon estrogen dan progesteron dengan kekuatan yang berbeda. Erosi serviks lebih sering terjadi pada wanita yang meminumpil dengan tingkat estrogen tinggi.</p>
								</li>
								<li>
									<p>Erosi serviks yang ditemukan pada semua bayi perempuan. Ini adalah respons terhadap estrogen ibu bahwa bayi yang menjadi sasaran saat masih berada didalam rahim. Erosi tersebut akan hilang pada masa kanak-kanak karena pengaruh menurunnya hormon.</p>
								</li>
								<li>
									<p>Terapi penggantian hormon pada saat menopause terutama terdiri dari pergantian estrogen dalam tubuh melalui pil dan lain-lain. Estrogen ini dapat menyebabkan erosi serviks.</p>
								</li>
								<li>
									<p>Erosi serviks jarang terlihat pada wanita setelah menopause karena tingkat estrogen dalam darah sangat rendah.</p>
								</li>
							</ul>
						</div>

						<p>
							<dol class="red-text">Infeksi:</dol> Infeksi adalah penyebab erosi serviks. Bukti meningkat bahwa infeksi tidak menyebabkan erosi serviks tapi justru sebaliknya. Sel-sel yang berubah dari erosi serviks lebih rentan terhadap berbagai bakteri dan ragi dan cenderung terinfeksi dengan sangat mudah. Jadi, erosi serviks dan infeksi serviks terjadi secara bersamaan tentunya yang menyebabkan bahwa infeksi adalah salah satu penyebab erosi serviks.
						</p>

