                            Apa Komplikasi Polip Serviks (Leher Rahim)?
                        </p>

                        <p>
                            Komplikasi mengenai polip serviks (leher rahim) dapat diamati melalui hal-hal seperti berikut:
                        </p>

                        <div class="chitietbaiviet-lienquan container card">
                            <ul>
                                <li>Gangguan emosional karena kekhawatiran untuk kanker serviks (leher rahim)</li>
                                <li>Polip yang dapat mengalami ulserasi (suatu penyakit radang usus kronis yang menyebabkan peradangan disaluran pencernaan) dan berdarah yang menyebabkan infeksi sekunder</li>
                                <li>Masalah kesuburan karena kanal serviks (leher rahim) yang dapat menghalangi aliran sperma yang mudah</li>
                                <li>Tumor polypoid terdapat cedera yang dapat menyebabkan rasa sakit yang luar biasa</li>
                                <li>Kerusakan pada otot, saraf vital dan pembuluh darah selama operasi </li>
                                <li>Infeksi pasca bedah dilokasi luka merupakan komplikasi yang sangat potensial</li>
                            </ul>
