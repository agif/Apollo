                            Efek Jangka Pendek Dan Jangka Panjang Dari Polip Serviks (Leher Rahim)
                        </p>

                        <div class="chitietbaiviet-lienquan container card">
                            <ul>
                                <li>Penyebaran kanker ke jaringan dan organ yang berdampingan seperti dinding panggul (pelvic), kandung kemih, dan rektum (tempat penyimpanan sementara feses) akan menghasilkan gejala yang terkait</li>
                                <li>Kanker yang dapat mempengaruhi ke organ lain seperti hati, paru-paru dan tulang. Hati yang membesar (hepatomegali) dapat terjadi pada kasus yang melibatkan hati. Mempengaruhi paru dan dapat menyebabkan efusi pleura (penumpukan cairan diantara jaringan yang melapisi paru-paru dan dada) atau obstruksi bronkus.</li>
                                <li>Pada wanita dengan kanker stadium awal yang berkeinginan memiliki anak kemungkinan ingin mempertahankan rahim dan memilih untuk perawatan bedah selain histerektomi.</li>
                                <li>Kegagalan dalam perawatan yang dapat terjadi pada beberapa wanita dimana kanker yang tidak dapat menerima segala bentuk terapi.</li>
                                <li>Bahaya atau komplikasi yang terkait dengan operasi, terapi radiasi atau kemoterapi.</li>
                                <li>Kanker serviks dapat kambuh setelah perawatan.</li>
