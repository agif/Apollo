                                Pengobatan Polip Serviks
                            </p>
                            <p>
                                Terkadang polip serviks akan melepaskan diri dari serviks (leher rahim) dengan sendirinya. Hal ini bisa terjadi saat seorang wanita sedang menstruasi atau saat melakukan hubungan seksual. menghilangkan polip serviks adalah prosedur sederhana yang dapat dilakukan dokter. Metode untuk menghilangkan polip serviks meliputi:
                            </p>

                            <div class="chitietbaiviet-lienquan container card">
                                <ul>
                                    <li>Memutar polip serviks didasar</li>
                                    <li>Mengikat tali bedah disekitar pangkal polip serviks dan memotongnya untuk meminimalkan pendarahan</li>
                                    <li>Menggunakan ring forcept untuk menghilangkan polip serviks</li>
                                    <li>Dilatase dan kuretase</li>
                                </ul>
                            </div>

                            <p>
                                Anda kemungkinan akan merasakan nyeri yang ringan sesaat saat pengangkatan dan kram ringan sampai sedang selama beberapa jam sesudahnya. Spotting darah dari vagina bisa terjadi selama satu atau dua hari setelah pemindahan. Dalam beberapa kasus, polip serviks atau bstsng polip serviks terlalu besar untuk dilepas. Jika demikian, Anda harus menjalani operasi untuk menghilangkan polip serviks.
                            </p>

