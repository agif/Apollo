                            <p class="font-weight-bold">
                                Bagaimana Polip Serviks Didiagnosis Dan Diobati
                            </p>

                            <p>
                                Dokter akan menemukan polip serviks selama pemeriksaan panggul secara rutin dan pap smear, dan akan mengambil sampel jaringan (biopsi) lalu mengirimkan ke laboratorium untuk dipastikan kalau itu bukan kanker. Dengan menggunakan alat yang disebut tang polip untuk memantul perlahan pertumbuhan dari serviks (leher rahim) Anda.
                            </p>

                            <p>
                                jika belum jelas? Kemungkinan akan berdarah dan ada kram sedikit selama proses pengobatan. Namun jika polip serviks Anda besar, dokter akan menyarankan untuk melakukan proses operasi dengan menggunakan anestesi lokal atau umum. Sebagian besar polip serviks jinak, tidak akan mneimbulkan masalah dan tidak akan kembali begitu sudah diangkat polip serviks nya.
                            </p>

