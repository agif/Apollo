                            <p class="font-weight-bold">
                                Pengobatan Polip Serviks Dengan Operasi
                            </p>
                            <p>
                                Polip serviks diangkat secara operasi dengan menggunakan alat khusus. Hasi pemeriksaan polip serviks dikirim ke laboratorium untuk diperiksa. Jika menunjukkan tanda-tanda infeksi, maka polip serviks itu bersifat kanker, dan pengobatan akan tergantung pada serviks (leher rahim). Pemeriksaan panggul secara teratur akan membantu mengidentifikasi dan mengobati polip serviks sebelum menimbulkan gejala.
                            </p>
                            <p>
                                Polip yang menyebabkan pendarahan atau keputihan dan dikeluarkan saat pemeriksaan panggul. Tapi biasanya tidak terjadi pendarahan saat polip serviks diangkat. Jika gejala (pendarahan dan keputihan) tetap ada setelah polip serviks diangkat, sampel jaringan dari lapisan rahim (endometrium) diambil dan diperiksa dibawah mikroskop (biopsi endometrium) untuk menyingkirkan kanker endometrium.
                            </p>

                            <div class="chitietbaiviet-lienquan container card">
                                <ul>
                                    <li>Jika polip serviks nya kecil maka seringkali bisa dioperasi untuk dihilangkan sampai jaringan polip serviks keluar dari serviks (leher rahim)</li>
                                    <li>Setelah adanya pemindahan polip serviks, kemungkinan adanya pendarahan dan sedikit kram selama beberapa hari</li>
                                    <li>Anda harus menjalani tes pap smear 3 bulan setelah polip telah dihilangkan, meskipun tes terakhir terlihat normal</li>
                                    <li>Blok polip mengakses semua sel dan hasil noda bisa jadi negatif</li>
                                    <li>Polip serviks bisa kambuh atau tumbuh kembali konsultasikan dengan dokter ketika gejalanya kembali</li>
                                </ul>
                            </div>

                            <p class="font-weight-bold">
                                Mengapa Sangat Penting Untuk Menghilangkan Polip Serviks?
                            </p>

                            <div class="chitietbaiviet-lienquan container card">
                                <ul>
                                    <li>Menghilangkan polip serviks akan mengurangi gejala seperti pendarahan setelah berhubungan seksual atua pendarahan antara periode menstruasi</li>
                                    <li>Dan jika dibiarkan polip serviks bisa terus tumbuh</li>
                                    <li>Polip serviks bisa menjadi abnormal dan berkembang menjadi kanker</li>
