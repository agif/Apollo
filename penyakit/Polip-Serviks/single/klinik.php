                            <p>
                                Polip serviks adalah pertumbuhan pada kanal serviks, bagian yang menghubungkan rahim ke vagina. Polip serviks yang berwarna kemerahan, keunguan atau berwarna keabu-abuan. Polip serviks kemungkinan berbentuk seperti jari, bohlam atau batang penis. Dan ukurannya bisa berkisar antara beberapa milimeter sampai beberapa sentimeter.
                            </p>
                            <p>
                                Serviks adalah saluran tubelike yang menghubungkan rahim ke vagina. Polip serviks adalah pertumbuhan yang biasanya muncul di serviks (leher rahim) dimana ia membuka celah untuk masuk ke dalam vagina. Polip serviks biasanya berwarna merah atau kemerah-merahan atau ke ungu-unguan atau putih dan ke abu-abu an. Polip serviks sangat bervariasi dalam ukurannya dan sering terlihat seperti umbi pada batang yang tipis.
                            </p>
                            <p>
                                Polip serviks biasanya tidak bersifat kanker (jinak) dan bisa terjadi sendiri atau dalam kelompok. Sebagian besar polip serviks berukuran kecil, sekitar 1 sentimeter sampai 2 sentimeter. Karena jenis kanker langka dapat terlihat seperti polip serviks, maka semua polip serviks harus dihilangkan dan diperiksa untuk tanda-tanda kankernya agar bisa segera disembuhkan.
                            </p>
