						</p>
						<p>
							Polip serviks adalah tumor pedunculat yang biasanya timbul dari mukosa kanal endoserviks. Biasanya berwarna merah dan memiliki struktur yang lembut dan kenyal. Atau pertumbuhan seperti jari dibagian bawah rahim yang terhubung dengan vagina. Penonjolan kecil yang tumbuh di serviks (leher rahim). Polip akan tampak sebagai gejala peradangan kronis atau infeksi pada serviks (leher rahim). Atau akibat penumpukan di pembuluh darah serviks atau dari kenaikan kadar estrogen.
						</p>
