                        <p class="font-weight-bold">
                            Apa Saja Yang Menjadi Faktor Risiko Untuk Penyakit Radang Panggul (PID)?
                        </p>
                        <p>
                            Faktor risiko meningkatkan kemungkinan penyebaran penyakit radang panggul (PID) misalnya pada setiap wanita yang menggunakan IUD sebagai bentuk pengendalian kelahiran dan akan berisiko lebih besar untuk penyakit radang panggul (PID). Bukan berarti setiap wanita yang menggunakan IUD dapat terkena penyakit radang panggul (PID) namun yang menggunakannya lebih cenderung menyebarkan kondisinya. Beberapa faktor risiko penyakit radang panggul (PID) diantaranya:
                        </p>

                        <div class="chitietbaiviet-lienquan container card">
                            <ul>
                                <li>Riwayat penyakit menular seksual (PMS) terutama gonore (kencing nanah dan klamidia)</li>
                                <li>Penyakit radang panggul (PID) sebelumnya</li>
                                <li>Usia remaja yang aktif secara seksual 3 kali lebih mungkin terkena dari pada wanita dengan usia 25 tahun sampai 29 tahun</li>
                                <li>Beberapa pasangan seksual dan semakin besar jumlah pasangan semakin besar risikonya karena meningkatnya risiko infeksi serviks (leher rahim)</li>
                                <li>Sering melakukan hubungan seksual berisiko tinggi mengalami infeksi penyakit radang panggul (PID)</li>
                                <li>Setiap wanita yang menggunakan IUD untuk mengendalikan kelahiran akan lebih mungkin untuk menyebarkan penyakit radang panggul (PID), peningkatan risiko hanya ada pada wanita yang juga berisiko terkena penyakit menular seksual (PMS). IUD dapat digunakan dengan aman dalam hubungan monogami</li>
                                <li>Infeksi HIV. Wanita dengan HIV lebih rentan terhadap infeksi saluran genital yang lebih rendah yang dapat menyebar ke panggul dan meningkatkan risiko penyakit radang panggul (PID)</li>
                            </ul>
                        </div>

                        <p>
                            Penyakit radang panggul (PID) biasanya lebih parah dan lebih sulit diobati pada wanita dengan infeksi HIV. Karena HIV menghambat kemampuan sistem kekebalan tubuh untuk melawan infeksi. wanita yang terinfeksi HIV lebih cenderung memerlukan perawatan bedah untuk penyakit radang panggul (PID). Wanita dengan HIV harus memeriksakan pap smear reguler untuk infeksi vagina dan serviks (leher rahim). Wanita yang terinfeksi HIV sebaiknya tidak menggunakan IUD (alat kontrasepsi) karena IUD akan menyebabkan bakteri untuk tetap masuk kesaluran reproduksi bagian atas.
                        </p>
                       
