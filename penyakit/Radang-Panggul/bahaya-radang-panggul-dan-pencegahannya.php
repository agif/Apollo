                        <p class="font-weight-bold">
                            Pencegahan Penyakit Radang Panggul (PID)
                        </p>

                        <p>
                            Untuk mengurangi risiko terkena penyakit radang panggul (PID) dengan mengurangi risiko tertular infeksi menular seksual (IMS). Cara terbaik adalah dengan menahan diri dari seks dan melakukannya hanya dengan monogami atau hanya dengan 1 pasangan. Atau menggunakan pengaman atau kondom lateks dan spermisida (alat kontrasepsi yang mengandung bahan kimia yang digunakan untuk membunuh sperma) ketika melakukan hubungan seksual.
                        </p>

