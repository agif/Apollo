                        <p class="font-weight-bold">
                            Parahnya Bahaya Penyakit Radang Panggul (PID)
                        </p>

                        <p>
                            Penyakit radang panggul (PID) dapat menyebar diluar saluran reproduksi yang menyebabkan komplikasi serius dan berpotensi mematikan. Terlepas dari bahaya langsung, efek jangka panjang penyakit radang panggul (PID) bisa sangat menghancurkan. Penyakit radang panggul (PID) dapat menjadi permanen dan merusak saluran tuba dan menyebabkan penyumbatan pada tabung. 
                        </p>
                        <p>
                            Wanita yang menderita cukup kerusakan pada tuba dari penyakit radang panggul (PID) menjadi tidak subur. Dan akan mencapai tingkat infertilitas atau kemandulan. Penyakit radang pangul (PID) juga dapat meningkatkan risiko kehamilan ektopik atau hamil anggur dimana sel telur yang dibuahi terjebak dalam tabung dan akan mulai tumbuh disana.
                        </p>

