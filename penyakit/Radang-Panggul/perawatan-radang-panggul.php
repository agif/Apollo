                                Perawatan Yang Dilakukan Untuk Penyakit Radang Panggul (PID)
                            </p>

                            <p>
                                Suatu bakteri yang menyebabkan adanya penyakit radang panggul (PID) dapat menyebar melalui kontak seksual. Jika Anda aktif secara seksual, pasangan Anda juga harus segera diobati dan ditangani untuk penyakit radang panggul (PID). Setiap pria kemungkinan merupakan pembawa bakteri yang diam yang menyebabkan adanya penyakit radang panggul (PID).
                            </p>

                            <p>
                                Infeksi bisa kambuh jika pasangan Anda tidak menerima Perawatan. Dan disarankan untuk menjauhkan diri dari melakukan hubungan seksual sampai infeksinya benar-benar disembuhkan. Untuk memastikan infeksinya benar-benar sembuh total, temui dokter 2 atau 3 hari seletah selesai melakukan Perawatan. Karena untuk memastikan Perawatannya bekerja atau tidak.
                            </p>

                            <p>
                                Jika penyakit radang panggul (PID) didiagnosis pada tahap awal yang biasanya berlangsung selama 14 hari, maka perawatan penykait radang panggul (PID) sangat penting untuk diselesaikan keseluruhannya dan menghindari hubungan seksual selama proses perawatan untuk memastikan infeksi hilang. Pasangan seksual terakhir Anda juga perlu melakukan pengujian dan tes laboratorium untuk menghentikan infeksi berulang atau menyebar ke orang lain.
