                                Bagaimana Cara Merawat Penyakit Radang Panggul (PID)
                            </p>
                            <p>
                                Setelah dilakukan pemeriksaan, dokter akan mengetahui jenis bakteri yang menjadi penyebab infeksi penyakit radang panggul (PID). Dalam beberapa hari setelah perawatan dimulai, gejala yang mulai membaik dan hilang, dan perawatan harus dilakukan sampai selesai, bahkan jika Anda sudah merasa lebih baik jangan menghentikan perawatan diawal karena dapat menyebabkan infeksi kembali atau berulang.
                            </p>
                            <p>
                                Jika Anda sakit dan sedang hamil dan tidak dapat melakukan perawatan atau memiliki abses (kantong pus yang disebabkan oleh infeksi) di panggul Anda, maka dokter akan menyarankan Anda untuk segera melakukan Perawatan. Penyakit radang panggul (PID) kemungkinan memerlukan pembedahan dan ini sangat jarang terjadi jika abses di pelvis (panggul) Anda pecah atau diduga akan pecah. Hal seperti ini sangat diperlukan karena infeksi tidak merespons adanya Perawatan.
                            </p>

