                            <p>
                                Klinik Utama Sentosa menangani segala jenis penyakit yang berhubungan dengan penyakit kelamin. Salah satu tempat yang sangat sesuai ketika Anda ingin melakukan pengobatan penyakit kulit dan kelamin adalah di Klinik Utama Sentosa. Karena Klinik Utama Sentosa sangat mengutamakan kepuasan dan kesembuhan pasien. Dengan pelayanan yang sangat baik serta proses pengobatan yang persentase kesembuhannya mencapai hingga 98.5 %, tidak diragukan lagi untuk melakukan pengobatan penyakit kelamin Anda.
                            </p>
                            <p>
                                Penyakit radang panggul (PID) adalah peradangan pada satu atau lebih organ di area pelvis (panggul) wanita, seperti serviks (leher rahim), endometrium (lapisan rahim), saluran tuba atau ovarium (indung telur). Penyakit radang panggul (PID) disebabkan oleh infeksi bakteri yang seringkali merupakan infeksi menular seksual (IMS) seperti gonore atau klamidia.
                            </p>
                            <p>
                                Penyakit radang panggul (PID) juga bisa menghentikan kehamilan, pemasangan alat kontrasepsi atau intrauterine atau persalinan. Penyakit radang panggul (PID) adalah kondisi umum yang mempengaruhi sekitar 1 dari 8 wanita dikehidupan mereka. Penyakit radang panggul (PID) paling sering terjadi pada wnaita yang aktif secara seksual antara usia 20 dan 24 tahun. Jika penyakit radang panggul (PID) didiagnosis sejak dini, maka penyakit radang panggul (PID) mudah untuk diobati dan kebanyakan dari wanita akan sembuh.
