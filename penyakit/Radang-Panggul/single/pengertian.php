						<p class="font-weight-bold">
							Penyebab Radang Panggul
						</p>

						<p>
							Berbagai jenis bakteri dapat menyebabkan penyakit radang panggul (PID) seperti klamidia dan gonore (kencing nanah) adalah 2 jenis infeksi menular seksual (IMS) yang umum. Terkadang infeksi yang mengarah ke penyakit radang panggul (PID) adalah akibat bakteri yang dimasukkan ke dalam vagina atau saluran kelamin atas selama persalinan, aborsi atau keguguran, atau ketika melakukan prosedur untuk mengambil sampel jaringan dari dalam rahim (biopsi endometrium).
						</p>
						<p>
							Penyakit radang panggul (PID) dapat berkembang sebagai akibat dari radang usus buntu, pengobatan serviks (leher rahim) atau setelah pemasangan IUD (alat kontrasepsi). Penyebab infeksi kemungkinan bakteri biasa yang tidak berbahaya yang ditemukan di vagina. Bakteri ini terkadang bisa melewati serviks dan masuk ke organ reproduksi. Meski tidak berbahaya bagi vagina, bakteri jenis ini bisa menyebabkan infeksi pada bagian tubuh yang lain. Infeksi ini kemungkinan besar terjadi bila terjadi kerusakan pada serviks (leher rahim) atau jika pernah menderita penyakit radang panggul (PID) sebelumnya.
						</p>

