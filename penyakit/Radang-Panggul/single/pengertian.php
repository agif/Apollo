						<p class="font-weight-bold">
							Apa Yang Menjadi Gejala Pada Penyakit Radang Panggul (PID)?
						</p>
						<p>
							Terkadang sulit untuk mengetahui apakah menderita penyakit radang panggul (PID) atau tidak, karena beberapa dari wanita yang kemungkinan memiliki gejala yang parah sementara orang lain kemungkinan memiliki gejala yang ringan atau bahkan tidak sama sekali.
						</p>

