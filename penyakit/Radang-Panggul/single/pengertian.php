						<p class="font-weight-bold">
							Bagaimana Radang Panggul Di Tes Dan Didiagnosis?
						</p>
						<p>
							Penyakit radang panggul (PID) sulit untuk didiagnosis karena gejala yang bervariasi secara signifikan dari wanita ke wanita. Dokter akan melakukan pemeriksaan dan menanyakan gejala termasuk melakukan pemeriksaan panggul (pelvic). Setelah itu mengambil sampel dari vagina dan serviks (leher rahim) selama pemeriksaan panggul (pelvic) untuk mengetahui mikrooganisme yang dapat menyebabkan infeksi.
						</p>
						<p>
							Tes urine bisa dilakukan untuk menguji secara khusus gonore (kencing nanah), klamidia dan sejenis bakteri yang disebut mycoplasma genitalium. Atau dilakukan tes tambahan untuk menentukan tingkat infeksi. Infeksi klamidia adalah salah satu penyebab tersumbatnya saluran tuba. Wanita yang terinfeksi dengan klamidia seringkali tidak memiliki gejala pada awal infeksi, jika aktif secara seksual.
						</p>

