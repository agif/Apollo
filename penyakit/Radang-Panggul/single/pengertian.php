							Apa itu Radang Panggul?
						</p>
						<p>
							Penyakit radang panggul (PID) adalah infeksi yang dapat mempengaruhi rahim, ovarium dan saluran tuba. Jika Anda memiliki penyakit radang panggul (PID) yang tidak diobati maka akan menyebabkan penyumbatan atau adhesi. Adhesi adalah tempat jaringan yang saling menempel, sesuatu yang dapat menyebabkan masalah pada kesuburan jika terjadi disekitar tuba fallopi.
						</p>
						<p>
							Penyakit radang panggul (PID) adalah infeksi. Jika seorang wanita mengalami infeksi di serviksnya (masuk ke rahim), bakteri penyebab infeksi bisa bergerak ke atas ke dalam rahim dan menyebar ke tuba fallopi dan ovarium (indung telur). Penyakit radang panggul (PID) disebabkan oleh lebih dari 1 jenis bakteri dan terkadang sulit dilakukan tes untuk menentukan bakteri mana penyebabnya.
						</p>

