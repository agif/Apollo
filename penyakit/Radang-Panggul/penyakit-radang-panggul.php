							Apa Yang Menjadi Komplikasi Pada Penyakit Radang Panggul (PID)?
						</p>
						<p>
							Komplikasi penyakit radang panggul (PID) termasuk sulitnya hamil (infertilitas atau kemandulan), nyeri panggul kronis, pembentukan jaringan parut didalam tuba falopi dan kehamilan ektopik. Bahkan sejumlah kecil jaringan parut di tuba falopi dapat mengganggu kesuburan dengan mencegah sel telur agar tidak dibuahi mencapai rahim. Jika sel telur yang telah dibuahi mulai berkembang di tuba falopi, maka tabung ini bisa pecah dan menyebabkan keadaan darurat yang mengancam jiwa dengan pendarahan internal dan rasa sakit yang parah. 
