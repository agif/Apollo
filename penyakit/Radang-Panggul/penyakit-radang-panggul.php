							Apa Yang Menyebabkan Penyakit Radang Panggul (PID)?
						</p>

						<p>
							Dua infeksi menular seksual (IMS) seperti gonore dan klamidia yang merupakan penyebab utama penyakit radang panggul (PID). Gonore dan klamidia dapat menyebabkan gejala samar atau bahkan tidak ada gejala pada wanita. Ketika seorang wanita terinfeksi dengan gonore atau klamidia dan tidak mendapat Perawatan, bisa terjadi beberapa hari sampai beberapa minggu sebelum penyakit radang panggul (PID) menyerang. Penyakit radang panggul (PID) juga bisa disebabkan oleh infeksi yang tidak menular secara seksual seperti bakteri vaginosis.
						</p>

