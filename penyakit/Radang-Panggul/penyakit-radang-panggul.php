							Apa Itu Penyakit Radang Panggul (PID)?
						</p>
						<p>
							Penyakit radang panggul (PID) mengacu pada infeksi rahim, saluran tuba (tabung yang membawa telur dari ovarium ke rahim) dan organ reproduksi lainnya yang menyebabkan gejala seperti sakit perut bagian bawah. Hal seperti ini adalah salah satu komplikasi yang sangat serius dari beberapa penyakit menular seksual (PMS0, terutama klamidia dan gonore.
						</p>
						<p>
							Penyakit radang panggul (PID) dapat merusak tuba falopi dan jaringan didalam dan dekat rahim dan ovarium. Penyakit radang panggul (PID) dapat menyebabkan konsekuensi serius termasuk ketidaksuburan, kehamilan ektopik (hamil anggur atau kehamilan di tuba falopi atau ditempat lain diluar rahim), pembentukan abses, dan nyeri pada panggul kronis.
						</p>

