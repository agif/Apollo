                            <p class="font-weight-bold">
                                Penyebab Radang Panggul Yang Disebabkan Oleh Klamidia Dan Gonore
                            </p>

                            <p>
                                Berbagai jenis bakteri dapat menyebabkan penyakit radang panggul (PID). Namun, penyebab yang paling sering adalah 2 infeksi menular seksual yang umum yaitu klamidia dan gonore. Bakteri yang menyebabkan klamidia (klamidia trachomatis) bertanggung jawab atas 50-65 % kasus penyakit radang panggul (PID). Bakteri yang menyebabkan gonore (neisseria gonorroeae) bertanggung jawab atas sekitar 14% kasus. Sekitar 8% wanita dengan penyakit radang panggul (PID) terinfeksi dengan klamidia dan gonore.
                            </p>
                            <p>
                                Kadang-kadang infeksi yang mengarah ke penyakit radang panggul (PID) dapat muncul sebagai akibat bakteri yang dimasukkan ke dalam vagina atau saluran kelamin atas selama persalinan, aborsi atau keguguran, atau prosedur untuk mengambil sampel jaringan dari dalam rahim (biopsi endometrium). Dalam kasus yang jarang terjadi, penyakit radang panggul (PID) dapat berkembang sebagai akibat dari radang usus buntu, perawatan serviks (leher rahim) atau setelah pemasangan IUD (alat kontrasepsi).
                            </p>
                            <p>
                                Dalam beberapa kasus penyebab infeksi yang menyebabkan penyakit radang panggul (PID) tidak diketahui. Kasus seperti itu kemungkinan mengakibatkan bakteri biasa yang tidak berbahaya yang ditemukan di vagina. Bakteri ini terkadang bisa melewati serviks (leher rahim) dan masuk ke organ reproduksi. Meski tidak berbahaya bagi vagina, bakteri jenis ini bisa menyebabkan infeksi pada bagian tubuh yang lain. Infeksi dengan cara ini kemungkinan besar terajdi bila telah terjadi kerusakan pada serviks (leher rahim) atau jika pernah menderita penyakit radang panggul (PID) sebelumnya.
                            </p>

