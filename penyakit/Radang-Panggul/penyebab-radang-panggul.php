                                Apa Yang Menjadi Penyebab Penyakit Radang Panggul (PID Atau Pelvic)?
                            </p>
                            <p>
                                Sejumlah mikroorganisme berbeda dapat menyebabkan atau berkontribusi pada penyakit radang panggul (PID). Patogen (mikroorganisme parasit) menular seksual klamidia trachomatis dan neisseria gonorroeae telah terlibat dalam sepertiga sampai setengah kasus penyakit radang panggul (PID). Mycoplasma genitalium kemungkinan juga breperan dalam penyakit radang panggul (PID) dan kemungkinan terkait dengan gejala ringan.
                            </p>
                            <p>
                                Penyakit radang panggul (PID) adalah infeksi. Jika seorang wanita mengalami infeksi di serviksnya (masuk ke rahim), bakteri penyebab infeksi bisa bergerak ke atas ke dalam rahim dan menyebar ke tuba falopi dan ovarium (indung telur). Penyakit radang panggul (PID) sering disebabkan oleh lebih dari satu jenis bakteri dan terkadang sulit dilakukan tes yang dilakukan dokter untuk menentukan bakterinya.
                            </p>

