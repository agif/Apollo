                            <p class="font-weight-bold">
                                Penyebab Umum Penyakit Radang Panggul (PID)
                            </p>
                            <p>
                                Penyebab paling umum penyakit radang panggul (PID) adalah organisme mikro menular seksual yang dapat menyebabkan penyakit seperti gonore dan klamidia. Mikroorganisme yang menyebabkan penyakit menular seksual (PMS) ini diteruskan melalui organ seksual dari pasangan pria ke pasnagan wanita dengan ditularkan ke vagina dan serviks (leher rahim) saat bersenggama.
                            </p>
                            <p>
                                Dari situ mikroorganismenya bisa melakukan perjalanan ke organ reproduksi internal wanita. Bakteri dalam infeksi yang sedang berkembang juga biasanya ditemukan di usus besar. Hal seperti ini terjadi jika bakteri usus mendapat akses ke vagina, terutama jika hubungan seksual terjadi tepat setelah melakukan hubungan seks anal. Yang kurang umum, penyakit radang panggul (PID) dapat disebabkan oleh organisme lain seperti TBC dan jarang disebabkan oleh schistosomiasis dan kusta.
                            </p>
                            <p>
                                Penyakit radang panggul (PID) disebabkan oleh infeksi bakteri di bagian saluran reproduksi bagian atas. Organ yang paling sering terkena meliputi tuba falopi, ovarium dan rahim. Bakteri yang paling sering dikaitkan dengan penyakit radang panggul (PID) adalah neisseria gonorrhoeae dan klamidia trachomatis, namun bakteri yang biasanya ada di vagina dan serviks (leher rahim) juga dapat dilibatkan. Serviks (leher rahim) menghasilkan lendir yang biasnaya mencegah bakteri menyebar ke saluran reproduksi bagian atas. 
                            </p>

