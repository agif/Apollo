                                Gejala Penyakit Radang Panggul (PID) Tergantung Pada Penyebabnya
                            </p>

                            <p>
                                Penyakit radang panggul (PID) bisa datang dengan cepat dengan adanya rasa sakit dan demam yang ekstrem terutama jika salah satu penyebabnya adalah penyakit gonore atau kencing nanah. Gejala penyakit radang panggul (PID) sangat bervariasi dari ringan sampai parah. Namun penyakit radang panggul (PID) yang tidak diobati dan tidak ditangani dapat menimbulkan konsekuensi yang serius.
                            </p>

                            <p>
                                Terkadang gejala penyakit radang panggul (PID) sangat mirip dengan kista ovarium (kantung padat atau berisi cairan kista didalam atau pada permukaan ovarium (indung telur), radang usus buntu, endometriosis (suatu gangguan pada jaringan yang biasanya melapisi rahim tumbuh diluar rahim) atau infeksi saluran kemih (ISK). Penyakit radang panggul (PID) bisa akut dan berlangsung selama hingga 30 hari atau kronis jika berlangsung lebih dari 30 hari. 
                            </p>

                            <p>
                                Salah satu kesulitan untuk Merawat penyakit radang panggul (PID) adalah bahwa gejalanya bervariasi dan beberapa dari kebanyakan wanita kemungkinan tidak memiliki gejala. Siapapun yang mengalami gejala atau yang mengira bahwa mereka terkena infeksi menular seksual (IMS) atau penyebab infeksi lainnya harus segera menemui dokter.
