                                Apa Tanda Dan Gejala Pada Radang Panggul (PID)
                            </p>
                            <p>
                                Beberapa atau kebanyakan dari wanita tidak mengetahui bahwa mereka menderita penyakit radang panggul (PID) karena mereka tidak memiliki tanda dan gejala. Bila gejala memang terjadi maka gejalanya bisa ringan atau lebih serius dengan pelvic inflammatory disease (PID) atau penyakit radang panggul, penyakit radang panggul (PID) dapat menyebabkan rasa sakit ringan atau sedang. Namun bagi wanita yang memiliki rasa sakit dan gejala yang parah, dan gejalanyabisa meliputi:
                            </p>

                            <div class="chitietbaiviet-lienquan container card">
                                <ul>
                                    <li>Adanya rasa nyeri yang tajam pada perut bagian bawah dan panggul (pelvic) ini adalah gejala yang paling umum</li>
                                    <li>Keputihan pada vagina dengan bau yang tidak sedap</li>
                                    <li>Pendarahan uterus (organ otot) yang tidak normal, terutama selama atau setelah hubungan seksual atau antara siklus menstruasi</li>
                                    <li>Adanya rasa nyeriatau pendarahan saat bersenggama</li>
                                    <li>Demam tinggi dan terkadang menggigil (suhu tinggi)</li>
                                    <li>Sakit dan sulit buang air kecil (kencing)</li>
                                    <li>Kelelahan</li>
                                    <li>Mual dan muntah</li>
                                    <li>Pingsan</li>
                                    <li>Sakit saat berhubungan seksual</li>
                                    <li>Periode menstruasi yang tidak teratur</li>
                                    <li>Adanya bercak atau kram sepanjang bulan</li>
                                    <li>Keputihan abnormal yang terkadang berwarna kuning atau hijau </li>
                                    <li>Ketidaknyamanan terasa jauh didalam panggul (pelvic)</li>
                                    <li>Rasa nyeri di perut bagian kanan atas (ini jarang terjadi)</li>
