                                Apa Yang Menjadi Penyebab Salpingitis?
                            </p>
                            <p>
                                Salpingitis biasanya disebabkan oleh infeksi bakteri yang didapat melalui hubungan seks per vagina. Bakteri yang paling umum pada penyakit salpingitis meliputi:
                            </p>

                            <div class="chitietbaiviet-lienquan container card">
                                <ul>
                                    <li>Klamidia</li>
                                    <li>Gonococcus (yang menyebabkan gonore atau kencing nanah)</li>
                                    <li>Mikoplasma</li>
                                    <li>Staphylococcus</li>
                                    <li>Strestococcus</li>
                                </ul>
                            </div>

                            <p>
                                Bakteri harus mendapatkan akses ke sistem reproduksi wanita agar terjadi infeksi. Bakteri bisa menyebar dengan berbagai cara meliputi:
                            </p>

                            <div class="chitietbaiviet-lienquan container card">
                                <ul>
                                    <li>Hubungan seksual</li>
                                    <li>Pemasangan AKDR (perangkat intra urine)</li>
                                    <li>Keguguran</li>
                                    <li>Abortus</li>
                                    <li>Persalinan</li>
                                    <li>Radang usus buntu</li>
                                </ul>
                            </div>

                            <p>
                                Adanya infeksi yang berasal dari vagina lalu naik ke tuba fallopi.Namun karena infeksi bisa menyebar melalui pembuluh getah bening, maka infeksi pada salah satu tuba fallopi biasanya dapat  menyebabkan infeksi yang lain muncul. Salpingitis hampir selalu disebabkan oleh infeksi bakteri, yang bisa terjadi saat bakteri ke saluran reproduksi wanita. Hal seperti ini terjadi dalam beberapa cara yang mungkin:
                            </p>

                            <div class="chitietbaiviet-lienquan container card">
                                <ul>
                                    <li>Infeksi menular seksual (IMS) seperti klamidia atau gonore (kencing nanah)</li>
                                    <li>Memasukkan perangkat intra uterin (IUD)</li>
                                    <li>Persalinan</li>
                                    <li>Abortus</li>
                                    <li>Radang usus buntu</li>
                                </ul>
