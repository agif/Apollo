                            <p class="font-weight-bold">
                                Infeksi Bakteri Yang Menyebabkan Salpingitis
                            </p>

                            <p>
                                Tuba fallopi memanjang dari rahim dan satu disetiap sisinya serta keduanya terbuka didekat ovarium (indung telur). Selama ovulasi (proses yang terjadi didalam siklus menstruasi wanita), sel telur yang dilepaskan (ovum) memasuki tuba fallopi dan tersapu rambut ke rahim. Salpingitis adalah radang saluran tuba. Hampir semua kasus yang disebabkan oleh infeksi bakteri, termasuk penyakit menular seksual (PMS) seperti gonore (kencing nanah) dan klamidia. 
                            </p>
                            <p>
                                Peradangan mendorong sekresi cairan ekstra atau bahkan nanah untuk dikumpulkan didalam tuba fallopi. Infeksi satu tabung biasanya menyebabkan infeksi pada yang lain, karena bakteri tersebut bermigrasi melalui pembuluh getah bening didekatnya. Salpingitis adalah salah satu penyebab paling umum infertilitas atau kemandulan pada wanita. Tanpa perawatan segera, infeksi dapat merusak tuba fallopi secara permanen sehingga telur yang dilepaskan setiap siklus menstruasi tidak dapat bertemu dengan sperma.
