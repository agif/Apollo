                            <p class="font-weight-bold">
                                Penyebab Salpingitis Yang Paling Umum
                            </p>

                            <p>
                               Pada kebanyakan kasus, salpingitis disebabkan oleh infeksi bakteri. Jenis bakteri yang umum menyebabkan salpingitis adalah mycoplasma, staphylococcus dan streptococcus. Namun, hal seperti itu juga bisa disebabkan oleh penyakit menular seksual (PMS) seperti gonore (kencing nanah) dan klamidia.
                            </p>
                            <p>
                                Infeksi sering dimulai di vagina dan menyebar ke atas ke tuba fallopi. Karena infeksi dapat menyebar melalui pembuluh getah bening, infeksi pada tuba fallopi juga dapat menyebabkan infeksi pada tuba fallopi lain. Telah diperkirakan bahwa infeksi tersebut mencapai tuba fallopi selama aliran menstruasi yang retrograde dan saat pembukaan serviks (leher rahim) saat menstruasi.
                            </p>
                            <p>
                                Faktor risiko meliputi prosedur pembedahan yang mengganggu batas serviks (leher rahim), seperti biopsi endometrium, kuretase (pemotongan jaringan yang berada disekitar rahim) dan histeroskopi. Faktor-faktor yang mengubah lingkungan mikro di vagina dan serviks (leher rahim) dan mendorong organisme yang menginfeksi untuk berkembang biak dan kemudian naik ke tuba fallopi juga meningkatkan risiko salpingitis. Faktor-faktor ini antara lain: perawatan dengan antibiotik, ovulasi (proses yang terjadi didalam siklus menstruasi wanita), menstruasi dan penyakit menular seksual (PMS).
                            </p>
                            <p>
                                Infeksi pada genitor organ kemih menyebabkan perkembangan salpingitis. Dalam kasus maksimum, selama hubungan seksual yang tidak aman menyebabkan penularan infeksi ke dalam rahim dan dari situ berpindah ke tuba fallopi. Beberapa pasangan hubungan seksual atau mereka yang secara aktif berpartisipasi dalam aktifitas seksual terutama menderita salpingitis. Operasi yang tidak disterilkan, termasuk persalinan anak dan bahkan pemasangan AKDR dapat menyebabkan salpingitis akut. Infeksi bakteri terutama menyebabkan salpingitis.
                            </p>

