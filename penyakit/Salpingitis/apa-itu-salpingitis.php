                                Bagaimana Salpingitis Didiagnosis?
                            </p>

                            <big>Mendiagnosis salpingitis melibatkan sejulah tes termasuk:</big>
                            <div class="chitietbaiviet-lienquan container card">
                                <ul>
                                    <li>Pemeriksaan umum, untuk memeriksa pembesaran kelenjar getah bening</li>
                                    <li>Pemeriksaan panggul, untuk memeriksa keputihan</li>
                                    <li>Tes darah, untuk memeriksa jumlah sel darah putih dan faktor lain yang mengindikasikan infeksi</li>
                                    <li>Lendir (mukus), pemeriksaan pap smear diperlukan untuk mengetahui jenis bakteri yang dapat diidentifikasi</li>
                                    <li>Laparoskopi, saluran tuba yang kemungkinan akan disisipkan melalui sayatan perut</li>
                                </ul>
