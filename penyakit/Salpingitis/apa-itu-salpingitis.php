                            <p class="font-weight-bold">
                                Apa Yang Menjadi Penyebab Salpingitis?
                            </p>

                            <p>
                                Salpingitis disebabkan oleh infeksi menular seksual (IMS) yang sering terjadi pada jenis klamidia yaitu klamidia trachomatis yang infeksinya biasanya dimulai di vagina dan naik ke saluran genital atas. Patogen (mikroorganisme parasit) lain yang kemungkinan ditularkan secara seksual dan menyebabkan jenis peradangan ini adalah gonococcus dan mycoplasma. Salpingitis muncul dengan cara yang bervariasi. Dan bisa muncul setelah melakukan kontak dengan patogen (mikroorganisme parasit).
                            </p>

