                            <p class="font-weight-bold">
                                Apa Yang Menjadi Komplikasi Pada Salpingitis
                            </p>

                            <big>Tanpa perawatan sesegera mungkin, salpingitis dapat menyebabkan berbagai komplikasi diantaranya:</big>
                            <div class="chitietbaiviet-lienquan container card">
                                <ul>
                                    <li>Infeksi lebih lanjut, infeksi yang dapat menyebar ke struktur terdekat seperti ovarium (indung telur) atau rahim</li>
                                    <li>Infeksi pasangan seks, pasangan wanita yang dapat memiliki bakteri dan menjadi terinfeksi</li>
                                    <li>Abses (kantong sempit berisi nanah yang berkumpul di jaringan, organ atau ruang didalam tubuh) tubo dan ovarium (indung telur)</li>
                                    <li>Kehamilan ektopik, tuba fallopi yang tersumbat mencegah sel telur yang dibuahi memasuki rahim. Embrio kemudian mulai tumbuh didalam ruang tertutup tuba fallopi. Risiko kehamilan ektopik untuk wanita dengan salpingitis sebelumnya atau bentuk penyakit radang panggul (PID) lainnya</li>
                                    <li>Infertilitas atau kemandulan, tuba fallopi menjadi cacat atau bekas luka sedemikian rupa sehingga sel telur dan sperma tidak dapat bertemu</li>
                                </ul>
                            </div>

