                                Definisi Selpingitis
                            </p>
                            
                            <p>
                                Salpingitis adalah kondisi dimana saluran tuba menjadi terinfeksi dan meradang. Kondisi ini juga dikenal sebagai penyakit radang panggul (PID), meski hal ini bisa mencakup tidak hanya gejala salpingitis tapi juga endometriosis (suatu gangguan pada jaringan yang biasanya melapisi rahim tumbuh diluar rahim) atau ooforitis (radang ovarium atau indung telur). Karena salpingitis sering dipicu oleh infeksi bakteri dan pada kasus yang lebih parah dapat menyebabkan kerusakan parah pada saluran tuba yang menyebabkan ketidaksuburan pada wanita.
                            </p>
                           
