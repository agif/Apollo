                            <p class="font-weight-bold">
                                Adapun Gejala Pada Penyakit Salpingitis 
                            </p>

                            <p>
                                Gejala salpingitis yang lebih ringan, salpingitis kemungkinan tidak memiliki gejala. Ini berarti tuba fallopi bisa rusak tanpa wanita itu menyadari adanya infeksi. Gejala salpingitis bisa meliputi:
                            </p>

                            <div class="chitietbaiviet-lienquan container card">
                                <ul>
                                    <li>Keputihan vagina yang tidak normal seperti warna atau bau yang tidak biasa</li>
                                    <li>Adanya bercak diantara periode menstruasi</li>
                                    <li>Dismenorea (periode yang sangat nyeri atau nyeri haid yang seringkali daialami wanita pada saat menjelang dan selama menstruasi)</li>
                                    <li>Sakit saat ovulasi (proses yang terjadi didalam siklus menstruasi wanita)</li>
                                    <li>Ketidaknyamanan dan menyakitkan saat berhubungan seksual</li>
                                    <li>Demam dan menggigil</li>
                                    <li>Nyeri perut di kedua sisi</li>
                                    <li>Sakit punggung bagian bawah</li>
                                    <li>Sering buang air kecil (urgensi kencing)</li>
                                    <li>Mual dan muntah</li>
                                    <li>Gejalanya bisa muncul setelah periode menstruasi</li>
                                </ul>
                            </div>

