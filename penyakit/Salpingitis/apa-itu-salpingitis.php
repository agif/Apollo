                                Faktor Risiko Dari Salpingitis
                            </p>

                            <big>Serangkaian situasi dan faktor yang dapat meningkatkan risiko pengembangan patologi yang paling umum adalah:</big>
                            <div class="chitietbaiviet-lienquan container card">
                                <ul>
                                    <li>Laki-laki dengan infeksi genital</li>
                                    <li>Operasi pelvis (panggul) sebelumnya</li>
                                    <li>IUD atau alat kontrasepsi</li>
                                    <li>Mempunyai riwayat penyakit radang panggul (PID)</li>
                                    <li>Berganti-ganti pasangan seksual</li>
                                </ul>
                            </div>

