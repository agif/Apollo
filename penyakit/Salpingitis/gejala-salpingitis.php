                                Apa Yang Menjadi Gejala Pada Salpingitis?
                            </p>

                            <p>
                                Pada kasus yang lebih ringan, salpingitis kemungkinan tidak memiliki gejala. Dan berarti tuba fallopi bisa rusak ketika atau tanpa wanita itu sendiri menyadarinya telah ada infeksi. Gejala salpingitis bisa meliputi:
                            </p>

                            <div class="chitietbaiviet-lienquan container card">
                                <ul>
                                    <li>Keputihan pada vagina yang tidak normal seperti warna atau bau yang tidak biasa</li>
                                    <li>Bercak diantara periode menstruasi</li>
                                    <li>Dismenorea (periode nyeri)</li>
                                    <li>Nyeri saat ovulasi (proses yang terjadi didalam siklus menstruasi wanita)</li>
                                    <li>Hubungan seksual yang tidak nyaman atau menyakitkan</li>
                                    <li>Demam</li>
                                    <li>Sakit perut di kedua sisi</li>
                                    <li>Nyeri punggung</li>
                                    <li>Sering buang air kecil (urgensi kencing)</li>
                                    <li>Mual dan muntah</li>
                                    <li>Gejalanya biasanya muncul setelah periode menstruasi</li>
                                </ul>
                            </div>

                            <p>
                                Salpingitis biasanya dikategorikan sebagai aku dan kronis. Pada salpingitis akut, tuba fallopi menjadi merah dan bengkak dan mengeluarkan cairan ekstra sehingga dinding dalam tabung sering menempel. Tabung juga menempel pada struktir terdekat seperti usus. Dan terkadang, tuba fallopi terisi dengan nanah. Dan dalam kasus yang jarang sekali terjadi, tabung pecah dan menyebabkan infeksi berbahaya pada rongga perut (peritonitis). Salpingitis kronis biasanya mengikuti serangan akut. Infeksi yang lebih ringan, tahan lama dan kemungkinan tidak menghasilkan banyak gejala yang nyata.
                            </p>

