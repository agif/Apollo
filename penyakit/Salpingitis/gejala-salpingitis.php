                            <p class="font-weight-bold">
                                Tanda Dan Gejala Salpingitis Akut
                            </p>
                            <p>
                                Nyeri perut bagian bawah, demam, nyeri serviks (leher rahim) dan pendarahan uterus yang tidak biasa sering terjadi terutama selama atau setelah menstruasi. Pada kasus salpingitis yang ringan, gejala kemungkinan tidak ada. Saat gejala muncul, biasanya munculnya setelah menstruasi. Kebanyakan wanita memiliki gejala seperti sakit perut bagian bawah atau atas, pendarahan yang tidak normal, sakit saat buang air kecil (BAK atau kencing), mual dan muntah, sakit saat hubungan intim, demam, namun beberapa wanita kemungkinan tidak memiliki gejala sama sekali.
                            </p>

                            <div class="chitietbaiviet-lienquan container card">
                                <ul>
                                    <li class="red-text"><big>Sevicitis</big>
                                        <p class="black-text">
                                            Serviks (leher rahim) merah dan mudah berdarah. Keputihan mucopurulen yang sering terjadi biasanya berwarna kuning kehijauan dari saluran endoserviks (bagian dalam dari leher rahim).
                                        </p>
                                    </li>
                                    <li class="red-text"><big>Salpingitis Akut</big>
                                        <p class="black-text">
                                            Sakit perut bagian bawah biasanya ada dan bilateral tapi kemungkinan hanya sepihak, bahkan saat kedua tabung terlibat. Nyeri juga bisa terjadi diperut bagian atas. Mual dan muntah sering terjadi saat rasa sakit nya sudah parah. Pendarahan tidak teratur (disebabkan oleh endometriosis atau suatu gangguan pada jaringan yang biasanya melapisi rahim dan tumbuh diluar rahim) dan demam namun untuk demam jarang sekali terjadi pada pasien
                                        </p>
                                    </li>
                                </ul>
                            </div>

                            <p>
                                Pada tahap awal tanda nya bisa ringan atau tidak ada. Nyeri yang tertekan pada serviks (leher rahim), terkadang dispareunia (suatu nyeri pada genital yang selalu menetap atau sering terjadi berulang-ulang ketika berhubungan intim, dari segi fisiologis tentu akan menyebabkan tekanan pada penderita secara personal) atau disuria juga terjadi. Kebanyakan pada wanita yang mengalami peradangan yang cukup parah dan menyebabkan parut yang memiliki gejala atau tidak ada gejala. Penyakit radang panggul (PID) karena gonorrhoeaea biasanya lebih akut dan menyebabkan gejala yang lebih parah daripada yang disebabkan oleh trachomatis.
                            </p>

