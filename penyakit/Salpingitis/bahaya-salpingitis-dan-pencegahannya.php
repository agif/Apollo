                        <p class="font-weight-bold">
                            Bahaya Atau Komplikasi Pada Penyakit Salpingitis
                        </p>
                        <p>
                            Dalam beberapa kasus, nanah akan terakumulasi dalam tuba fallopi (saluran telur) yang terinfeksi, dapat menciptakan kondisi yang disebut pyosalpinx (adanya nanah di salah satu tuba fallopi atau saluran telur). Selain itu, potensi cairan untuk dikumpulkan didalam tuba fallopi (saluran telur) yang meradang, adalah suatu kondisi yang dikenal sebagai hydrosalpinx (kondisi dimana terjadi sumbatan pada saluran telur wanita (tuba fallopi) dan terisi cairan (hidro).
                        </p>
                        <p>
                            Tuba fallopi (saluran telur) itu sendiri akan pecah dan dapat menyebabkan peritonitis (radang membran yang melapisi dinding perut dan menutupi organ-organ perut) infeksinya dapat berbahaya. Jika nanah berkumpul di rongga perut maka bisa menyebarkan abses ke daerah panggul (pelvic). Salpingitis juga dapat menyebabkan infertilitas atau kemandulan karena tuba fallopi (saluran telur) yang dapat terjadinya lubang atau cacat untuk melengkapi interaksi telur dan sperma.Bahaya atau komplikasi salpingitis adalah salah satu kondisi sekunder, gejala atau gangguan lain yang disebabkan oleh salpingitis. Perbedaan antara gejala salpingitis dan komplikasi salpingitis tidak akan jelas.
                        </p>
                       
