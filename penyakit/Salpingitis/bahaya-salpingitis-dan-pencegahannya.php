                            Apa Saja Kemungkinan Dari Bahaya Atau Komplikasi Penyakit Salpingitis
                        </p>

                        <div class="chitietbaiviet-lienquan container card">
                            <ul>
                                <li>Bahaya atau komplikasi akut pada penyakit salpingitis adalah terjadinya infeksi yang sangat berat dan diperlukan perawatan sesegera mungkin ketika Anda mengalami gejala di awal</li>
                                <li>Abses di panggul (bisul atau kantong sempit berisi nanah yang berkumpul di jaringan, organ, atau ruang di dalam tubuh)</li>
                                <li>Peritonitis (infeksi atau peradangan pada rongga perut)</li>
                                <li>Infertilitas atau kemandulan yang bahkan risiko utamanya adalah disfungsi tuba karena jaringan parut pada salpingitis tuba</li>
                                <li>Kehamilan ektopik (hamil diluar rahim atau hamil anggur) dan memiliki penyakit salpingitis yang berlipat ganda oleh 10 risiko kehamilan ektopik(hamil diluar rahim atau hamil anggur)</li>
                                <li>Kekambuhan penyakit salpingitis</li>
                                <li>Salpingitis kronik yang dilakukan dengan perawatan awal yang tidak adekuat (tidak memenuhi syarat)</li>
                                <li>Adanya rasa atau nyeri pada panggul (pelvic) yang kronis</li>
                                <li>Aborsi si awal</li>
