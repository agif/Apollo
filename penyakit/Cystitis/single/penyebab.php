							Penyebab Cystitis
						</p>
						<p>
							Penyakit dengan kondisi yang bisa menyebabkan radang yang disebut cystitis. Penyebab yang paling umum dari cystitis adalah infeksi yang disebabkan oleh bakteri. Penyebab umum lainnya termasuk batu kandung kemih, tumor atau polip dikandung kemih, dan anatomi abnormal. Sebagian besar infeksi kadnung kemih disebabkan oleh berbagai strain e.coli, bakteri yang biasanya hidup di usus.
						</p>
						<p>
							Wanita terkadang terkena infeksi kandung kemih setelah berhubungan seks. Hubungan seksual vagina memudahkan bakteri mencapai kandung kemih melalui uretra (saluran yang menghubungkan kantung kemih ke lingkungan luar tubuh). Beberapa wanita terjangkit infeksi dan hampir setiap mereka berhubungan seks.  Beberapa orang yang mengalami gejala cystitis disebut cystitis intertisial.
						</p>
						<p>
							Meskipun bakteri tidak berbahaya yang ada di usus untuk membantu pencernaan makanan di tubuh, bakteri dapat memasuki saluran kemih dimana mereka berkembang biak dan menyebar ke jaringan serta menyebabkan infeksi. Ada berbagai penyebab cystitis. Salah satunya yang paling umum bahwa cystitis terjadi ketika bakteri uropatogen dari usus mencapai kandung kemih dengan bergerak dari pembukaan usus (anus) ke dalam tabung yang membawa air kencing dari kandung kemih (uretra) ke luar tubuh.
						</p>
						<p>
							Beberapa wanita yang berisiko tinggi terkena cystitis daripada pria karena anatomi wanita membuatnya lebih memungkinkan bakteri menyebar dari usus ke saluran kemih. Ini karena uretra (saluran yang menghubungkan kantung kemih ke lingkungan luar tubuh) dan anus lebih pendek dan berdekatan. Anda juga bisa berisiko mengalami cystitis jika aktif secara seksual, hamil dan diabetes. Wanita yang telah mengalami manopause juga berisiko tinggi mengalami cystitis.
