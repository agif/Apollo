						<p class="font-weight-bold">
							Apa Saja Gejala Pada Cystitis?
						</p>

						<p>
							Gejala cystitis bervariasi dari setiap orang. Orang yang sama kemungkinan memiliki gejala yang berbeda pada waktu yang berbeda. Gejala sering memburuk saat menstruasi. Gejala umum bisa termasuk ketidaknyamanan yang ringan, tekanna dan nyeri seperti tertekan yang hebat pada kandung kemih dan daerah panggul (pelvic) di sekitarnya. Gejala umumnya termasuk:
						</p>

						<div class="chitietbaiviet-lienquan container card">
							<ul>
								<li>Buang air kecil (BAK atau kencing) yang terdesak</li>
								<li>Sering buang air kecil (urgensi kencing)</li>
								<li>Perubahan intensitas nyeri saat kandung kemih terisi atau kosong</li>
								<li>Dinding-dinding kandung kemih, bekas luka dan kaku</li>
								<li>Glomerulasi (pendarahan tepat yang disebabkan oleh iritasi berulang) pada dinding kandung kemih</li>
								<li>Ulkus pada dinding kandung kemih</li>
								<li>Sakit saat melakukan hubungan seksual atau aktifitas seksual lainnya</li>
							</ul>
						</div>
						
