						<p class="font-weight-bold">
							Bagaimana Cystitis Didiagnosis?
						</p>

						<div class="chitietbaiviet-lienquan container card">
							<ul>
								<li>Ketika gejalanya tidak membaik setelah 2 atau 3 hari</li>
								<li>Memiliki darah dalam urine (hematuria)</li>
								<li>Antara hamil atau tidak </li>
								<li>Berusia 65 tahun ke atas</li>
								<li>Memiliki suhu tinggi, merasa mual dan muntah</li>
								<li>Memiliki masalah lain dengan sistem saluran kencing seperti batu ginjal atau kesulitan mengosongkan kandung kemih</li>
								<li>Menderita diabetes</li>
							</ul>
						</div>

						<p>
							Cystitis pada pria dapat disebabkan oleh pembesaran prostat . Tanda-tanda cystitis jauh lebih sulit dikenali pada anka-anak, kemungkinan merasa demam dan merasa tidak enak badan. Dan segera melakukan pemeriksaan ke dokter untuk mencegah masalah ginjal di kemudian hari. Dokter akan mengambil sampel urine dan diuji di laboratorium.
						</p>

