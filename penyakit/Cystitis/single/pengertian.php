						</p>

						<p class="font-weight-bold">
							Apa Yang Menyebabkan Cystitis?
						</p>

						<p>
							Cystitis sering disebabkan oleh bakteri yang masuk ke uretra (saluran yang menghubungkan kantung kemih ke lingkungan luar tubuh) dari kulit sekitarnya dan melewati kandung kemih yang menyebabkan infeksi dan iritasi. Sebagian besar infeksi disebabkan oleh bakteri yang biasanya hidup tanpa membahayakan pada usus yang disebut escherichia coli (e.coli).
						</p>

						<p>
							Seorang wanita yang memiliki cystitis lebih banyak dibandingkan pria yang memilikinya, karena pada wanita uretra nya lebih dekat pada pembukaan bagian belakang (anus) dimana bakteri dari usus bisa berkumpul. Hal seperti ini dapat memudahkan bakteri untuk berpindah dari kulit sekitarnya ke dlaam uretra. Kemungkinan terkena cystitis jika:
						</p>

						<div class="chitietbaiviet-lienquan container card">
							<ul>
								<li>Aktif secara seksual yang risikonya semakin sering untuk berhubungan seksual</li>
								<li>Penggunaan kondom berlapis spermisida atau diafragma dengan spermisida karena dapat mengubah keasaan PH pelindung pada wanita</li>
								<li>Melewati masa menopause dan dapat menyebabkan perubahan pada lapisan vagina dan uretra, mengurangi elastisitas dan peningkatan PH, dan lebih mudah terkena infeksi bakteri</li>
								<li>Memiliki kateter urine dan bakteri bisa langsung ke kandung kemih</li>
								<li>Memiliki diabetes terutama jika penyakitnya tidak bisa terkontrol karena urine mengandung lebih banyak gula yang memberi makan bakteri dan mendorong untuk tumbuh</li>
								<li>Memiliki kondisi yang dapat mencegah untuk mengosongkan kandung kemih seperti batu kandung kemih atau batu ginjal, pembesaran prostat atau jika sedang hamil</li>
								<li>Bahan-bahan yang dapat mengiritasi uretra atau kandung kemih</li>
							</ul>
						</div>

						<p class="special-type">
							<a href="#" style="text-align: center;"><span class="special-rgb"><strong><img src="../../../img/icon/special-icon.gif"></strong></span>
