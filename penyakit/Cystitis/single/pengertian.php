							Pengobatan Cystitis
						</p>
						<p>
							Pengobatan yang memerlukan istirahat yang banyak dan meminum banyak air mineral. Atau mandi air hangat untuk meringankan gejala, berbaring di tempat tidur atau istirahat yang cukup. Serta segera lakukan pemeriksaan ke dokter agar segera diketahui penyebab pastinya, setelah adanya diagnosis dan melakukan pengobatan atau terapi. Semakin cepat diobati maka penyakitnya juga akan segera disembuhkan.
