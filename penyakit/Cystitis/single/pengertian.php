						<p class="font-weight-bold">
							Definisi Cystitis
						</p>
						<p>
							Cystitis adalah radang kandung kemih yang seringkali karena adanya infeksi bakteri. Dikenal sebagai infeksi saluran kencing (ISK). Hal seperti ini bisa diakibatkan karena hubungan seksual. Bakteri yang terlibat biasanya e.coli yang hidup di usus. Cystitis bisa disebabkan oleh: infeksi menular seksual (IMS), pengobatan radiasi, penggunaan obat-obatan tertentu dan beberapa bahan kimia atau spermisida (alat kontrasepsi yang mengandung bahan kimia yang digunakan untuk membunuh sperma).
						</p>
