                        <p class="font-weight-bold">
                            Bagaimana Cara Mencegah Infeksi Ginjal  
                        </p>

                        <p>
                            Cara terbaik untuk mencegah infeksi ginjal adalah dengan menjaga kandung kemih dan uretra (saluran yang menghubungkan kantung kemih ke area luar tubuh) bebas dari bakteri dengan cara:
                        </p>

                        <div class="chitietbaiviet-lienquan container card">
                            <ul>
                                <li>Meminum banyak cairan</li>
                                <li>Jangan menahannya jika ingin kencing</li>
                                <li>Kencing setelah berhubungan seks</li>
                                <li>Menyeka dari depan ke belakang ketika mencuci kelamin</li>
                                <li>Cuci alat kelamin setiap setiap hari dan sebelum melakukan hubungan seks jika mmeungkinkan</li>
                                <li>Obati sembelit yang dapat meningkatkan kemungkinan terkena infeksi saluran kemih (ISK)</li>
                                <li>Tidak menggunakan diafragma atau pengaman atau kondom yang dilapisi spermisida jika rentan terkena infeksi saluran kemih (ISK) dan diperkirakan spermisida dapat meningkatkan risiko infeksi saluran kemih (ISK)</li>
