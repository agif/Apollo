                        <p class="font-weight-bold">
                            Bagaimana Cara Menghindari Komplikasi Cystitis?
                        </p>

                        <p>
                            Selain efek samping dari cystitis Anda harus berhati-hati dalam kebersihan diri pribadi. Selangkangan harus tetap di seka dari pubis ke tulang ekor saat mencuci. Cuci setiap buang air besar (BAB), ganti selalu pembalut saat menstruasi. Pastikan perawatan inflamasi dalam tubuh. Hindari hipotermia (suatu kondisi dimana mekanisme tubuh untuk pengaturan suhu kesulitan untuk mengatasi tekanan suhu dingin).Perhatikan gaya hidup yang aktiif. Nutrisi yang penting dalam pencegahan penyakit jantung akan membantu mengurangi efek samping dari penyakit tersebut dan akan memudahkan kondisinya.
                        </p>

