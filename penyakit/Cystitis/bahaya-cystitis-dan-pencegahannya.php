                        <p class="font-weight-bold">
                            Bahaya Yang Terkait Dengan Cystitis?
                        </p>

                        <p>
                            Bahaya atau komplikasi yang paling serius dari penyakit cystitis atau alasan utama untuk Merawatnya adalah bahaya infeksi ginjal. Atau bisa disebut dengan pielonefritis adalah infeksi ginjal yang dapat melukai ginjal secara permanen. Semakin lama kondisinya tidak diobati maka akan semakin buruk.
                        </p>
                        <p>
                            Memiliki darah dalam urine tidak bisa dilihat oleh mata telanjang. Mengharuskan melakukan tes analisis urine agar bisa melihat darahnya. Jika kondisi seperti ini terus berlanjut bahkan setelah dilakukannya perawatan maka diharuskan menjalani tes lebih lanjut untuk menentukan penyebab darah dalam urine tersebut.
                        </p>
                        <p>
                            Jika menderita cystitis dan kemungkinan bakteri yang menyebabkan infeksi saluran kemih (ISK) akan resisten terhadap jenis perawatan yang paling umum. Merawat cystitis dengan terapi lebih agresif dan terkadang memiliki efek samping yang tidak diinginkan. Dan jika dilakukan terus terapi maka infeksinya bisa menurun.
                        </p>
                        
