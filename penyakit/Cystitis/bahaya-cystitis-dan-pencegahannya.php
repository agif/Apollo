                            Bahaya Penyakit Cystitis?
                        </p>
                        <p>
                            Pada wanita yang tidak hamil, biasanya infeksi yang ditimbulkan dikenal dengan infeksi ringan yang akan hilang dengan sendirinya dan sangat jarang dapat menyebabkan komplikasi. Namun pada kehamilan, karena kerentanan wanita hamil maka cystitis dapat berkembang dengan cepat menjadi infeksi ginjal (pielonefritis). Bahayanya dapat menyebabkan persalinan prematur, invasi bakteri ke aliran darah (sepsis) atau sindrom gangguan pernafasan.
                        </p>
                       
