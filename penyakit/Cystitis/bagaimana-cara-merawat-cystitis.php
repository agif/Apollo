                        <p class="font-weight-bold">
                            Tindakan Perawatan Cystitis Pada Wanita
                        </p>

                        <p>
                            Gejala cystitis paling awal biasanya merupakan ingin buang air kecil (kencing). Hal ini sangat memungkinkan untuk meringankan cystitis ringan jika segera dilakukan tindakan. Dokter akan menyarankan melakukan pengujian urine untuk memeriksa mikroorganisme yang ada. Gejala cystitis yang teratur atau parah karena gangguan yang mendasarinya seperti batu ginjal atau infeksi ginjal bisa menjadi pemicu.
                        </p>
                        <p>
                            Perawatan cystitis yang berkembang sebagai komplikasi atau terapi yang berfokus pada penanganan nyeri untuk menghilangkan iritasi kandung kemih. Jika ada tanda-tanda infeksi kandung kemih, segera konsultasikan dengan dokter sesegera mungkin sebelum masalah menjadi lebih serius. Kondisi lain dengan gejala serupa termasuk urethritis (radang uretra), cystitis interstitial (infeksi kandung kemih dengan penyebab yang tidak diketahui), batu kemih dan tumor kandung kemih.
                        </p>
                        <p>
                            Dokter akan menyarankan berbagai pertanyaan tentang riwayat kesehatan dan seksual untuk mengambil sampel urine untuk melihat tentang kandungan bakteri dan jenisnya. Gejalanya biasanya terlihat jelas dalam beberapa hari dalam setiap kasusnya. Dan perawatan yang dilakukan untuk menyembuhkan infeksi. perawatan untuk cystitis non infeksi tergantung pada penyebab utamanya.
                        </p>
