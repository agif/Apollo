                            <p class="font-weight-bold">
                                Apa Yang Menjadi Penyebab Menstruasi Tidak Teratur?
                            </p>
                            <p>
                                Siklus menstruasi bisa terganggu jika Anda memiliki ketidakseimbangan hormon estrogen dan progesteron. Bukan tidak biasa memiliki ketidakseimbangan hormon selama beberapa tahun setelah pubertas dan sebelum menopause. Hal ini dapat menyebabkan siklus menstruasi menjadi lebih panjang atau pendek. Periode nya yang kemungkinan akan menjadi lebih ringan atau lebih berat.
                            </p>
                            <p>
                                Sejumlah faktor yang kemungkinan mengakibatkan menstruasi tidak teratur. Sebagian besar berhubungan dengan roduksi hormon. Dua hormon yang mempengaruhi menstruasi adalah estrogen dan progesteron atau hormon yang mengatur siklus. Menstruasi tidak teratur terkait dengan kondisi yang disebut anovulasi atau ovulasi tidak terjadi selama silkus menstuasi dan biasanya karena ketidakseimbangan hormon yang parah. Terkadang menstruasi tidak teratur karena keridakseimbangan hormon yang lebih rendah. Kemungkinan masih berovulasi tapi waktu ovulasi bisa sangat bervariasi dari bulan ke bulan. Dipengaruhi oleh gaya hidup dan kondisi yang dapat mempengaruhi siklus menstruasi.
                            </p>

                            <div class="chitietbaiviet-lienquan container card">
                                <ul>
                                    <li>Retensi urine, sering berakibat konstipasi atau mengalami pembesaran prostat</li>
                                    <li>Diabetes, karena kadar gula yang tinggi dapat menyebabkan bakteri berkembang biak cepat dari biasanya</li>
                                    <li>Perubahan keseimbangan bakteri di saluran kemih seperti setelah penggunaan produk kimia yang beraroma kuat atau saat mengalami menopause</li>
                                </ul>
                            </div>

