                            <p class="font-weight-bold">
                                Ada 4 Penyebab Umum Untuk Periode Menstruasi Tidak Teratur
                            </p>
                            <p>
                                Rata-rata siklus menstruasi sekitar 28-35 hari, dan pendarahan menstruasi biasanya berlangsung antara 3-7 hari. Perlu diingat bahwa apa yang dimaksud dengan menstruasi periode normal atau rata-rata untuk satu orang bisa berbeda dengan yang dialami orang lain. Jika periode menstruasi tiba-tiba bervariasi dari rata-rata panjang dan arusnya maka itu pertanda bahwa menstruasi tidak beraturan.
                            </p>
                            <p>
                                Periode menstruasi kemungkinan akan muncul lebih awal dari perkiraan atau tidak muncul sama sekali. Berikut adalah 4 penyebab yang paling umum yang dapat menyebabkan periode menstruasi tertunda atau awal sehingga menyebabkan siklus menstruasi tidak teratur:
                            </p>

