                            <p class="font-weight-bold">
                                Faktor Yang Dapat Memicu Periode Menstruasi Tidak Teratur:
                            </p>

                            <div class="chitietbaiviet-lienquan container card">
                                <ul class="red-text">
                                    <li><big>Olahraga atau diet ekstrem</big>
                                        <p class="black-text">
                                            Berolahraga terlalu banyak bisa membuang waktu pendarahan menstruasi dan terkadang berhenti. Sudah biasa bagi atlit ketahanan nya, mengurangi berat badan, olahraga ekstrem, gangguan makan, atau penyakit yang lainnya memiliki efek yang sama.
                                        </p>
                                    </li>
                                    <li><big>Tekanan</big>
                                        <p class="black-text">
                                            Stress kronis atau bahkan kegelisahan jangka pendek mengenai maslaah tertentu bisa memunculkan ketidakseimbangan hormon dan menyebabkan periode mentruasi dan siklus tidak teratur.
                                        </p>
                                    </li>
                                    <li><big>Pil KB</big>
                                        <p class="black-text">
                                            Pil KB dapat membuat menstruasi lebih ringan atau menyebabkan terlewatnya masa menstruasi atau memiliki periode menstruasi yang kurang atau lebih atau bahkan tidak ada periode sama sekali.
                                        </p>
                                    </li>
                                    <li><big>Sindrom Ovarium Polikistik (PCOS)</big>
                                        <p class="black-text">
                                            Kondisi ini menyebabkan kista kecil terbentuk pada ovarium (indung telur) mengganggu ovulasi (proses yang terjadi didalam siklus menstruasi wanita) tidak teratur. Biasanya memiliki menstruasi yang tidak teratur selain menyebabkan kemandulan atau infertilitas juga dapat meningkatkan risiko diabetes dan penyakit jantung.
                                        </p>
                                    </li>
                                    <li><big>Penyakit Lainnya</big>
                                        <p class="black-text">
                                            Gangguan tiroid dapat menyebabkan periode menstruasi tidak teratur jika kadar hormon tiroid darah terlalu rendah atau terlalu tinggi. Kondisi kesehatan lainnya yang dapat menyebabkan siklus menstruasi tidak teratur meliputi penyakit mneular seksual (PMS), diabetes, fibroid (mioma uteri atau daging tumbuh non kanker dalam rahim yang dapat muncul selama tahun-tahun masa subur seorang wanita), gangguan makan dan endometriosis (suatu gangguan pada jaringan yang bisanya melapisi rahim tumbuh diluar rahim).
                                        </p>
                                    </li>
                                    <li><big>Usia</big>
                                        <p class="black-text">
                                            Saat remaja mulai mengalami menstruasi, siklus menstruasi kemungkinan tidak selalu sesuai jadwal setiap bulannya. Kemungkinan membutuhkan beberapa tahun untuk menyelesaikan sebuah pola. Selain itu, periode terjawab dan periode yang lebih ringan atau lebih beat sering terjadi pada wanita menjelang menopause.
                                        </p>
                                    </li>
                                </ul>
