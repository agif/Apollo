							Faktor Risiko Menstruasi Tidak Teratur
						</p>
						<p>
							Faktor risiko berbeda dengan usia dan memiliki kondisi yang lain yang dapat menjelaskan menorrhagia. Dalam siklus yang normal, pelepasan sel telur dari ovarium (indung telur) menstimulasi produksi progesteron tubuh, hormon wanita yang paling bertanggung jawab untuk menjaga menstruasi tetap teratur. Jika tidak ada sel telur yang dilepaskan maka progesteron yang tidak cukup dapat menyebabkan pendarahan menstruasi yang berat. 
						</p>
