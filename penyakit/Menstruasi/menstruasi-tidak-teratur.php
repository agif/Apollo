							Menorrhagia pada remaja putri biasanya disebabkan oleh anovulasi. Gadis remaja sangat rentan terhadap siklus anovulasi pada tahun pertama setelah menstruasi pertama. Menorrhagia pada wanita usia reproduksi yang lebih tua biasanya disebabkan oleh patologi uterus termasuk fibroid, polip dan adenomiosis. Namun, pada masalah lain, seperti kanker rahim, gangguan pendarahan, efek samping obat-obatan dan penyakit hati atau ginjal harus dikesampingkan.
