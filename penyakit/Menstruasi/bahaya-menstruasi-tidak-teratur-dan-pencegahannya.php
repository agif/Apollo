                        <p class="font-weight-bold">
                            Apa Yang Menjadi Bahaya Menstruasi Tidak Teratur?
                        </p>
                        <p>
                            Secara umum tidak berbahaya ketika memiliki periode menstruasi tidak teratur. Namun ketika memiliki periode menstruasi tidak teratur bisa sangat normal pada awal dan akhir saat reproduksi wanita misalnya saat mengalami menstruasi atau ketika mendekati menopause. Menstruasi tidak teratur bisa menjadi pertanda ketidakseimbangan hormon atau kelainan pada organ reproduksi wanita (rahim dan ovarium atau indung telur).
                        </p>
                        <p>
                            Menstrusi tidak teratur bisa mengakibatkan masalah dengan jumlah darah rendah atau anemia. Semua wanita pasti mempunyai masalah kekhawatiran tentang menstruasi tidak teratur, cara yang terbaik adalah segera konsultasikan dengan dokter karena akan ada banyak alasan yang berbeda untuk setiap mensruasi yang tidak teratur.Untuk menghindari kemungkinan ketika lapisan rahim yang menebal yang dapat menjadi risiko pengembangan kanker rahim. Beberapa hal yang harus diperhatikan agar terhindar dari menstruasi tidak teratur diantaranya:
                        </p>

                        <div class="chitietbaiviet-lienquan container card">
                            <ul>
                                <li><font class="red-text">Stress</font>
                                    <p>
                                        Stress adalah salah satu penyebab yang paling umum dari menstruasi tidak teratur. Stress dapat menyebabkan perubahan hormon tertentu dalam tubuh yang dapat mempengaruhi waktu dan aliran dari siklus menstruasi tidak teratur. Bisa juga karena tekanan atau yang berhubungan dengan stress, salah satunya dengan rileks agar tidak mempengaruhi menstruasi.
                                    </p>
                                </li>
                                <li><font class="red-text">Perubahan Berat Badan</font>
                                    <p>
                                        Jika Anda memakan makanan yang kaya akan karbohidrat yang tidak sehat dan berat badan Anda bertambah gemuk dan menghasilkan berbagai tingkat hormon tertentu yang mempengaruhi siklus haid. Mengkonsumsi alkohol secara berlebihan juga merupakan faktor penyebab menstruasi yang tidak teratur, terlalu banyak minum alkohol dapat menyebabkan kerusakan pada hati dan dapat mengganggu metabolisme hormon normalisasi menstruasi.
                                    </p>
                                </li>
                                <li><font class="red-text">Pola Tidur Tidak Beraturan</font>
                                    <p>
                                        Kebiasaan gaya hidup seperi makan tidak teratur dan jam tidur tidak menentu dapat mempengaruhi siklus haid dalam jangka panjang. Gangguan ini trekait gaya hidup lainnya seperti obesitas atau kegemukan juga bisa menjadi salah satu faktornya.
                                    </p>
                                </li>
                                <li><font class="red-text">Kontrol tingkat kehamilan</font>
                                    <p>
                                        Butuh waktu beberapa bulan agar tubuh terbiasa dengan dosis pil KB.
                                    </p>
                                </li>
                                <li><font class="red-text">Polycystic ovary syndrome (gangguan hormonal yang menyebabkan pembesaran ovarium atau indung telur dengan kista kecil ditepi luar)</font>
                                    <p>
                                        Komplikasi yang cukup umum pada wanita adalah kondisi yang ditandai dengan pembentukan kista di ovarium (indung telur) yang menyebabkan menstruasi tidak teratur. Gejala lainnya dari kondisi tersebut meliputi pertumbuhan rambut yang tidak normal atau ketombe, penambahan berat badan dan ketidaksuburan.
                                    </p>
                                </li>
                                <li><font class="red-text">Gangguan tiroid</font>
                                    <p>
                                        Hipotiroidisme atau hipertiroidisme, kondisi kesehatan fisik dengan kadar hormon tiroid yang abnormal juga dapat berkontribusi pada menstruasi yang tidak teratur.
                                    </p>
                                </li>
                                <li><font class="red-text">Endometriosis</font>
                                    <p>
                                        Suatu gangguan pada jaringan yang biasanya melapisi rahim tumbuh diluar rahim.
                                    </p>
                                </li>
                                <li><font class="red-text">Anemia</font>
                                    <p>
                                        Kurang darah atau kondisi ketika darah tidak memiliki sel darah merah yang sehat dan cukup atau suatu kondisi yang timbul dari rendahnya kadar hemoglobin dalam darah, tidak hanya menyebabkan kelelahan pada tubuh namun dapat mempengaruhi pola menstruasi yang dapat menyebabkan menstruasi tidak teratur.
                                    </p>
                                </li>
                            </ul>
                        </div>

                        <p>
                            Pada wanita dengan siklus menstruasi tidak teratur kemungkinan akan memiliki lebih dari 2 kali lipat risiko kanker ovaroium (indung telur) dibandingkan dengan wanita yang mengalami menstruasi normal setiap bulannya.
                        </p>
                       
