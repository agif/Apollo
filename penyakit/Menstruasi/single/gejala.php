                            <p class="font-weight-bold">
                                Gejala Menstruasi Tidak Teratur Periode Umum
                            </p>
                            <p>
                                Tanda utama menstruasi tidak teratur adalah pendarahan pada vagina. Ini adalah hasil dari perubahan hormonal yang mempersiapkan tubuh wanita untuk hamil. Itu rata-rata terjadi setiap 28 hari dan berlangsung antara 2 dan 7 hari. Ini adalah siklus menstruasi dan ini sedikit berbeda untuk setiap wanita. Menstruasi tidak selalu merupakan pertanda masa menstruasi terkadang kemungkinan berdarah tanpa menstruasi dan dikenal sebagai bercak.
                            </p>
                            <p>
                                Biasanya mengalami kram dan dikenal sebagai dismenore dan disebabkan oleh kandungan rahim saat bekerja melepaskan lapisannya. Beberapa wanita yang tidak pernah mengalami kram sementara yang lain mengalami rasa sakit dan kelelahan yang parah setiap bulannya. Beberapa kram biasanya trejadi, terutama pada masa remaja.
                            </p>
                            <p>
                                Kram parah bisa jadi tanda endometriosis, kondisi kesehatan reproduksi yang menyebabkan periode nyeri, kelelahan dan infertilitas atau kemandulan. Jika Anda mengalami pra menstruasi syndrom (PMS) atau kumpulan gejala yang beberapa wanita dapatkan sepanjang masa menstruasi tidak teratur. Bisa termasuk sakit kepala, sakit punggung, mudah tersinggung atau perubahan mood, merasa sedih atau emosional, kembung dan nyeri seperti tertekan pada payudara.
                            </p>
