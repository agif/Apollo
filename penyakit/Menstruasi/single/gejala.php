                            <p class="font-weight-bold">
                                Memahami Gejala Pra Menstruasi Syndrom (PMS)
                            </p>
                            <p>
                                Pra menstruasi syndrom (PMS) adalah suatu kondisi yang mempengaruhi emosi, kesehatan fisik dan perilaku wanita selama hari-hari tertentu dari siklus menstrausi dan umumnya sebelum menstruasi tidak teratur. Pra menstruasi syndrom (PMS) adalah kondisi yang sangat umum. Gejalanya mempengaruhi hingga 85 % wanita yang sedang haid. Sangat mengganggu beberapa aspek kehidupan ketika didiagnosis.
                            </p>
                            <p>
                                Gejala pra menstruasi syndrom (PMS) mulai 5 sampai 11 hari sebelum menstruasi dan biasanya hilang begitu menstruasi muncul. Penyebab pra menstruasi syndrom (PMS) tidak diketahui. Namun, hal yang terkait dengan perubahan kadar hormon seks dan serotonin pada awal siklus menstruasi tidak teratur. Kadar estrogen dan progesteron meningat pada waktu-waktu tertentu dalam sebulan.
                            </p>
                            <p>
                                Kenaikan hormon ini dapat menyebabkan perubahan mood, kecemasan dan mudah tersinggung. Steroid ovarium (indung telur) juga memodulasi aktifitas dibagian otak terkait dengan gejala pramenstruasi. Tingkat seretonin mempengaruhi mood. Seretonin adalah zat kimia di otak dan usus yang mempengaruhi suasana hati, emosi dan pikiran.
                            </p>
