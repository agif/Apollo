                            <p class="font-weight-bold">
                                Mengobati Masalah Menstruasi Tidak Teratur
                            </p>
                            <p>
                                Jenis pengobatan akan tergantung pada apa yang dapat menyebabkan masalah dengan siklus haid. Pil KB dapat meringankan gejala menstruasi, sekaligus mengatur aliran yang deras. Jika lebih berat atau lebih ringan dari aliran yang normal berhubungan dengan tiroid atau gangguan hormonal lainnya, ada kemungkinan mengalami keteraturan lebih setelah mulai mengganti hormon.
                            </p>
                            <p>
                                Dismenore kemungkinan berhubungan dengan hormon, namun kemungkinan memerlukan pengobatan yang lebih lanjut untuk mengatasi masalah ini. Tergantung pada pasaien dan situasi, rencana pengobatan akan bervariasi. Histerektomi adalah pilihan operasi yaitu pengangkatan rahim dan serviks (leher rahim). Wanita yang mengalami menstruasi tidak teratur dan tidak normal harus mencatat siklus menstruasi dan gejalanya menggunakan jurnal atau kalender.
                            </p>
                            <p>
                                Dokter yang mendiagnosis dan mengobati masalah sistem reproduksi wanita (termasuk menstruasi tidak teratur) disebut ginekolog. Pada wanita yang dewasa secara seksual harus menjalani pemeriksaan ginekologi setiap tahun untuk mencegah masalah menstruasi. Kram karena menstruasi, kembung, nyeri payudara dan gejala lainnya yang terkait dengan menstruasi tidak teratur dapat merespon teknis pengobatan sebagai berikut:
                            </p>

                            <div class="chitietbaiviet-lienquan container card">
                                <ul>
                                    <li>Harus mengkonsumsi makanan sehat yang mencakup banyak biji-bijian, buah-buahan, sayuran, sumber protein yang baik dan sumber kalsium yang baik</li>
                                    <li>Konsultasikan dengan dokter sebelum memulai olahraga apapun</li>
                                    <li>Cukup tidur setiap malam sehingga terbangun dengan perasaan baik</li>
                                    <li>Selama menstruasi sering mengganti pembalut setidaknya etiap 4-8 jam</li>
                                    <li>Gunakan pembalut untuk menyerap aliran menstruasi</li>
                                    <li>Hindari atau batasi kafein</li>
                                    <li>Makan makanan ringan yang sering</li>
                                    <li>Konsultasikan dengan dokter mengenai suplemen vitamin yang dapat membantu gejala menstruasi</li>
                                    <li>Kurangi asupan garam sesaat sebelum menstruasi untuk mengurangi kembung dan pembengkakan</li>
                                    <li>Batasi alkohol yang bisa meningkatkan perasaan depresi</li>
