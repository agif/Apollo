						<p class="font-weight-bold">
							Apa Yang Menjadi Gejala Pada Menstruasi?
						</p>

						<p>
							Setiap bulan lapisan rahim wanita menumpuk dan kemudian dikeluarkan saat saklus haid, kecuali saat hamil. Untuk mengeluarkan lapisan selama per bulannya, otot rahim mengencang (berkontraksi). Sangat membantu jaringan yang melapisi rahim untuk mengalir keluar dari tubuh bersamaan dengan darah melalui serviks (leher rahim) dan vagina.
						</p>
						<p>
							Kontraksi otot yang terkadang tidak hanya menyebabkan ketidaknyamanan ringan, namun terkadang sangat menyakitkan seperti kram. Rasa nyeri bisa mempengaruhi perut bagian bawah dan kemungkinan terasa di punggung atau di kaki. Atau bisa menyebabkan mual, munyah dan diare pada beberapa wanita, serta rasa sakit kepala atau ketidaknyamanan yang umum.
						</p>

