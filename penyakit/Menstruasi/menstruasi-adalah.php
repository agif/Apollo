						<p class="font-weight-bold">
							Apa Saja Yang Menyebabkan Menstruasi?
						</p>

						<p>
							Dipicu oleh hormon yang mengirim pesan ke rahim untuk mempersiapkan kemungkinan kehamilan. Hormon ini membuat lapisan rahim menjadi tebal sehingga jika sel telur dibuahi oleh sperma pria maka bisa membuahi dengan aman pada dinding rahim. Jika sel telur tidak dibuahi dan kehamilan terjadi, maka rahim akan menajdi lapisan yang tebal.
						</p>
						<p>
							Darah yang meninggalkan vagina selama periode ini disebabkan oleh lapisan rahim yang keluar. Proses ini terjadi setiap bulannya sampai usia 40 tahun atau 50 tahun. Jika hormon menurun dan menstruasi berhenti disebut menopause. Menstruasi dibedakan menjadi 2 jenis periode nyeri. Yaitu dismenore primer dan dismenore sekunder. 
						</p>
						<p>
							Jika nyeri disebabkan oleh kontraksi otot rahim saja disebut dismenore primer. Dan tidak diketahui mengapa kontraksi bisa sangat menyakitkan. Dan kemungkinan melibatkan produksi prostaglandin terlalu banyak atau kepekaan tertentu terhadapnya. Dan nyeri yang diakibatkan oleh sesuatu selain kontraksi otot disebut dismenore sekunder. 
						</p>
						<p>
							Pertumbuhan jinak pada rahim seperti fibroid dan polip menandakan dismenore sekunder. Endometriosis juga bisa menyebabkan nyeri yang parah. Pada endometriosis jenis jaringan yang melapisi rahim tumbuh ditempat lain. Terkadang alat kontrasepsi juga dapat menyebabkan dismenore sekunder.
						</p>

