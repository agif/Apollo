							Bagaimana Mensruasi Ditangani?
						</p>

						<p>
							Untuk meringankan sakit saat menstruasi adalah pil KB dan perawatan penghilang rasa sakit dan mengurangi rasa sakit. Pil KB mencegah ovulasi (proses yang trejadi didalam siklus menstruasi wanita). Pil KB juga bisa memiliki efek samping seperti sakit kepala dan mual. Dan terkadang meningkatkan risiko trombosit dan tentunya memiliki efek samping. Terkadang rasa sakitnya sangat buruk sehingga perawatan psikologis atau terapi sangat diperlukan dan harus dipertimbangkan.
