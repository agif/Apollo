                                Perawatan Untuk Menstruasi Tidak Teratur
                            </p>

                            <p>
                                Menstruasi yang tidak lancar dapat terjadi pada beberapa tahun pertama menstruasi atau saat menjelang menopause. Menstruasi yang tidak lancar atau tidak teratur tersebut disebabkan oleh hormon yang belum seimbang. Namun jika menstruasi tidak lancar atau tidak teratur terjadi bukan dibeberapa tahun pertama mengalami menstruasi atau menjelang menopause, maka masalah menstruasi tersebut bisa tergolong ke dalam beberapa tipe menstruasi tidak teratur. Seperti metrorrhagia, oligomenorrhea, menometrrhagia dan amenorrhea.
                            </p>

                            <div class="chitietbaiviet-lienquan container card">
                                <ul>
                                    <li>Metrorrhagia adalah pendarahan rahim yang abnormal yang terjadi diantara periode menstruasi dan tidak berhubungan dengan menstruasi normal.</li>
                                    <li>Oligomenorrhea adalah siklus haid yang memanjang. Jarak antara siklus menajdi lebih lama dari normal yaitu lebih dari 35 hari per siklusnya.</li>
                                    <li>Amenorrhea adalah kondisi dimana tidak terjadinya menstruasi pada wanita.</li>
                                    <li>Menometrorrhagia merupakan pendarahan menstruasi yang berlangsung lebih lama dengan volume darah yang keluar lebih banyak.</li>
                                </ul>
