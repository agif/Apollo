                                Apa itu vulvitis ?
                            </p>

                            <p>
                                Vulvitis adalah radang pada alat kelamin luar. Vulvitis yang sering dikaitkan dengan keputihan adalah pembengkakan yang melibatkan mukosa vagina. Vulvitis primer biasanya perpanjangan dari sekunder ke dalam vagina. Sistem yang terkena dampak reproduksi kulit atau eksoktrin. Vulvitis adalah bagian luar vagina (vulva) yang meradang.
                            </p>
                            <p>
                                Vulva yang gatal, serasa terbakar atau terasa sakit. Masalah seperti ini terjadi sebelum pubertas. Peradangan organ kelamin wanita yang eksternal (vulva) dikenal sebagai vulvitis. Penyebab vulvitis dan jenisnya tergantung pada penyebabnya. Biasanya vulvitis berhubungan dengan vaginitis yang sering dikenal dengan vulvovaginitis.
