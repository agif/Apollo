                            <p>
                                Vulvitis adalah radang dan infeksi pada vulva, organ luar vulva termasuk labia majora dan minora, klitoris dan kelenjar vestibular. Gejala dasar vulvitis adalah kemerahan, pembengkakan pada kulit vulva. Adanya sensasi gatal adalah gejala yang sangat menonjol dan konsisten. Area kulit vulva yang terkena bisa berubah menjadi warna putih dan adanya retakan atau lepuhan yang berisi cairan yang pecah dan berkerak.
                            </p>
