                                Bagaimana Cara Merawat Vulvitis?
                            </p>

                            <p>
                                Segera hentikan penggunaan produk yang dapat menyebabkan munculnya iritasi dan penggunaan pakaian dalam katun yang membuat tidak dapat bernapas atau sesak. Untuk mengurangi iritasi, gatal dan gejala vulvitis lainnya lakukan pemeriksaan dengan dokter. Jika tidak segera ditangani maka akan mengakibatkan kondisi yang lebih serius lagi seperti kanket vulva yang sangat parah.
                            </p>

