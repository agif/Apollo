                        <p class="font-weight-bold">
                            Faktor Risiko Dan Pencegahan Vulvitis Yang Terjadi Pada Remaja
                        </p>

                        <p>
                            Pengakit vulvitis adalah adanya peradangan pada vulva (bagian organ seksual ektsternal wanita yang merupakan area yang mengelilingi lubang kencing). Atau lipatan lembut pada kulit di luar alat kelamin wanita. Gejala yang bisa dihasilkan dari berbagai penyakit. Termasuk infeksi, cedera, alergi atau adanya iritasi.Karena dapat menjadi tantangan tersendiri mengenai penyebabnya yang tepat, serta mendiagnosis dan merawat kondisi seperti ini yang kemungkinan bisa sangat sulit.
                        </p>
                        <p>
                            Seorang remaja akan lebih berisiko terkena penyakit vulvitis jika memiliki alergi, kepekaan, infeksi atau penyakit tertentu yang dapat menyebabkan vulvitis. Pada setiap gadis yang belum mencapai pubertas dan pada wanita yang telah mengalami menopause akan lebih berisiko mengalami penyakit vulvitis kemungkinan karena kadar hormon yang lebih rendah.Penyakit vulvitis yang disebabkan oleh iritasi dapat dicegah dengan tidak bersentuan dengan penderitanya. Untuk melindungi anak-anak dari penyakit maka harus dijaga kebersihannya.
                        </p>
                        <p>
                            Selain riwayat medis yang lengkap dan pemeriksaan fisik dan panggul (pelvic), maka pemeriksaan penyakit vulvitis kemungkinan akan termasuk seperti berikut:
                        </p>

                        <div class="chitietbaiviet-lienquan container card">
                            <ul>
                                <li>Melakukan tes darah</li>
                                <li>Urinalisis atau tes urine</li>
                                <li>Pengujian atau tes untuk penyakit menular seksual (PMS) lainnya juga diperlukan</li>
                                <li>Tes pap smear atau tes yang melibatkan pemeriksaan miskropkopis sel yang dikumpulkan dari leher rahim (serviks), yang digunakan untuk mendeteksi perubahan yang kemungkinan itu kanker atau dapat menyebabkan munculnya kanker, dan untuk menunjukkan kondisi non kanker seperti infeksi atau peradangan</li>
                            </ul>
                        </div>

                        <p>
                            Perawatan khusus untuk penyakit vulvitis ditentukan berdasarkan:
                        </p>

                        <div class="chitietbaiviet-lienquan container card">
                            <ul>
                                <li>Usia, kesehatan secara keseluruhan dan riwayat kesehatan atau medis yang diderita sebelumnya</li>
                                <li>Penyebab penyakit yang mendasarinya</li>
                                <li>Jenis dan tingkat keparahan gejala yang dialami</li>
                                <li>Keinginan untuk sembuh dari penyakit tersebut juga dapat mempengaruhi proses jalannya perawatan</li>
                            </ul>
